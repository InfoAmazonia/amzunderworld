<!-- wp:heading {"level":1} -->
<h1 class="wp-block-heading" id="h-the-brotherhood-of-the-pcc"><strong><strong>THE ‘BROTHERHOOD’ OF THE PCC</strong></strong></h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Despite its military-sounding name, the PCC does not operate as a hierarchy, but rather as a brotherhood, says Gabriel Feltran, a sociology professor at the Federal University of São Carlos in the state of São Paulo, who has written a history of the group.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“The PCC does not operate with the logic of controlling, dominating territories, co-opting people, forcing them to work, extorting,” he says. “The PCC always works by the logic of convincing — convincing that it is useful, that it is good to be part of these networks, being a brother, and that it will be good for business, for their lives. It will be an alternative.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>That fluidity has helped the group expand and also helps protect it against government efforts to combat organized crime. And in its 30 years of existence, the PCC has been diversifying its business beyond drug trafficking.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“Several other markets that were not previously the focus of the group’s activities came into the picture in these 30 years. The market for gold, cryptocurrencies, wood, urban and rural land, events, soccer players ... where there is money and whoever has money can get in,” Feltran says.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“This way, they manage to have more access to weapons, lawyers, accountants, national and international networks, reaching the border areas — very important for national and international trade — reaching positions in retail, in wholesale, going from one state to another, making their alliances, meeting other people from international mafias, Italian, Russian, Mexican, Nigerian,” he adds,&nbsp;</p>
<!-- /wp:paragraph -->

