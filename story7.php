<p><strong>By <strong>Emily Costa, and Mariana Rios</strong></strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Miguel has a new job. It looks like he’s managing a café — in the place he oversees, a young man is serving coffee to a group of Brazilian boys, all of whom appear to be under age 18, who are sitting around a wooden table in a palm grove overlooking a small creek.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>But the boys aren’t there for the coffee. They’re after the drugs.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Miguel* came to Brazil from his native Venezuela half a dozen years ago to escape the economic, political and humanitarian crisis that has wracked his country for nearly a decade. And his new job, running a drug den in Boa Vista, in the northern Brazilian state of Roraima, is a reward from his employer.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>His boss is the Primeiro Comando da Capital (PCC), or First Capital Command, a criminal group born in São Paulo’s prisons in the 1990s, which has branches throughout the country, links to criminal groups in neighboring nations, and now a strong presence in the Amazon region. There, the PCC has diversified and found new ways of financing itself: Besides drug trafficking, it is active in illegal gold mining and illegal fishing.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>So how did a Venezuelan become a trusted gear in the machinery of a Brazilian criminal organization?&nbsp;</p>
<!-- /wp:paragraph -->

