<!-- /wp:image -->

<!-- wp:paragraph -->
<p>When he arrived, Miguel spent a few weeks in a house with other Venezuelans, sharing the space with other people in transit who needed affordable shelter, as well as some who were involved in the small- and large-scale drug trade. When police raided the house, he says, he was arrested along with other residents.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“They tied us to each other and made us walk through the streets of Pacaraima. I had never felt such shame in all my life,” he says.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>He ended up in Boa Vista’s Monte Cristo Agricultural Penitentiary, where he was housed in a cell block with PCC leaders. They considered themselves more civilized than the other groups, because they abided by a kind of code of conduct and mutual support.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Recruited in prison by the PCC, he began selling drugs for the criminal group after he was released. It’s a trade learned by example, he says of the “brotherhood”: “Anyone can talk, even a parrot can talk. Now I want to see how someone lives a life of crime.”.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Carlos Alberto Melott, a prosecutor in a group charged with combating organized crime in Roraima, has testified in court that Venezuelan inmates in Brazilian prisons are recruited by Brazilian criminal groups. He said investigators have also detected communication between low-level leaders of Brazil’s PCC and Venezuelans linked to that country’s Tren de Aragua, a criminal group that was born in the penitentiary in Tocorón, in the state of Aragua, about 130 kilometers from Caracas.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Like the PCC, the Venezuelan organization takes advantage of the flow of migrants to expand its influence, establishing alliances for drug and gold trafficking, as well as sexual exploitation of women. It is one of Venezuela’s main criminal organizations, operating not only in Roraima, but in other states in northern Brazil, and is alleged to have connections in Bolivia, Chile, Colombia, Ecuador and Peru, according to sociologist Rodrigo Chagas, a researcher at the Federal University of Roraima and the Brazilian Forum on Public Security, who studies criminal groups involved in gold mining in the Yanomami Indigenous Territory.</p>
<!-- /wp:paragraph -->

