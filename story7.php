<h1 class="wp-block-heading" id="h-a-convergence-of-crime"><strong><strong>A CONVERGENCE OF CRIME</strong></strong></h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>One place where drug trafficking, organized crime and illegal gold mining, known as <em>garimpo </em>in Portuguese, have converged is in the Yanomami Indigenous Territory, which shares a long border with Venezuela west of Boa Vista. For decades the area has been a magnet for illegal miners, or <em>garimpeiros</em>, but the PCC’s presence drew particular attention in 2021, when members of the group were involved in a series of attacks on Indigenous communities in the Palimiú region that were resisting invasions by miners.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>This year, the area has again been the scene of conflict and murders involving Indigenous people and miners. In the first week of May, there were at least 14 deaths around the Indigenous community of Uxiu, an area with a large number of <em>garimpeiros</em>. Among the victims were three Venezuelans, two men and a woman. The deaths occurred after miners attacked the Indigenous people. Three were shot and one died.</p>
<!-- /wp:paragraph -->

