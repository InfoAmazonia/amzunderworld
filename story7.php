<p>According to a survey published in 2022 by the Hutukara Yanomami Association, Brazilian criminals from the PCC infiltrated the miners in Roraima, first by providing security, and later by diversifying into managing freight services and houses of prostitution. The survey estimated that half of the Indigenous reserve’s nearly 30,000 inhabitants were directly affected by mining.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The São Paulo PCC faction controls houses of prostitution and services near clandestine mining ports and landing strips. At strategic points, they set up places known as <em>corrutelas</em> — often equipped with bars, brothels and internet service — to receive goods and people. It was in one of these places that Maria* worked as a prostitute in an illegal mining area near the Uraricuera River in the Yanomami Indigenous Territory.</p>
