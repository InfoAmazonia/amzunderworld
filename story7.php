<p>An attractive woman just over 20 years old, with manicured nails and numerous tattoos, she speaks nervously about a grotesque scene she witnessed. A <em>garimpeiro</em> had paid for sex and wanted to force a sex worker to marry him and dedicate herself exclusively to household tasks — washing clothes, cooking and having sex.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The young woman, who had gone to the mines to earn money as a prostitute, did not want to marry. She complained to the “owner” of the <em>currutela</em>, who executed the man in plain view with a single shot.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Maria left the region afterward, but she eventually returned and was dating a PCC member there. The tension was constant, though, and she was thinking of escaping to a more “family-oriented” mining area.&nbsp;</p>
