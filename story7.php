<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>— Gabriel Feltran</p>
<!-- /wp:paragraph --></blockquote>
<!-- /wp:quote --></div></div>
<!-- /wp:cover -->

<!-- wp:paragraph -->
<p>The group’s business operations in the gold mining regions are made possible by the amount of money circulating and a lack of oversight.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“If you earn 2 million <em>reais</em> [about $400,000] from cocaine trafficking, you can open and run your own restaurant. If you earn 200 million <em>reais</em> [about $40 million] over a few years, you can buy a <em>garimpo</em> and put a bunch of people to work for you there. And use that mining operation to launder your money, while producing gold and bringing wealth to you,” Feltran says. “And that's how the PCC arrived, not only in the Amazon, but in all Brazilian states and many other countries. In several regions of the world, several continents.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>He sees no difference between the attraction of young Brazilians and Venezuelans to the ranks of the PCC in northern Brazil.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“What matters is the person’s commitment to crime, the attitude towards his peers and prioritizing the faction over everything else, including his own family,” he says. “Because this is the environment, to be anti-system.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Nevertheless, the PCC does not have absolute control and faces opponents in the region.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“In the Amazon, there are many other armed groups that are not linked to the PCC, that are also opponents, like the FdN [Familia do Norte, which operates mainly in the Amazon], and the CV itself, which was allied with the PCC for a long time,” Feltran says. “And there are still armed groups that are local militias, linked to the police, colonels, local elites, landowners in the Amazon. So not everything that happens in the Amazon has to do with the PCC.”</p>
