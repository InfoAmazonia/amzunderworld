<h1 class="wp-block-heading" id="h-it-s-very-hard-in-the-mines"><strong><strong>‘IT’S VERY HARD IN THE MINES’</strong></strong></h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Yordan, a 23-year-old Venezuelan who spent six months as a <em>garimpeiro</em> in Brazil, said many migrants go to the mines through the city of Mucajaí in southern Roraima. Currently living in Manaus and working in construction, he says he never saw acts of violence or people carrying firearms during his time in the mines.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“Violence happens there when you do bad things,” he says, adding that people can stay out of trouble “if you work and don't mess with anyone.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>He sent part of his earnings from the mining to his family in Venezuela and used some to buy items like a television and refrigerator for the house where he lives in Manaus. But he doesn’t plan to go back to the mines.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“Not any more,” he says. “It’s very hard in the mines. I didn’t like it. I went there out of need. That’s why I went.”</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":164811,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/08/story7-ft2.jpeg" alt="" class="wp-image-164811"/><figcaption class="wp-element-caption">Venezuelans who recently crossed the border into Brazil await processing at the “Operation Welcome” center in Pacaraima. | Diane Sampaio</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>A 2021 report by the U.N. Office on Drugs and Crime (UNODC) warned of the risk of human trafficking in Brazil’s northern border region, noting especially the greater vulnerability of unaccompanied Venezuelan women, children and adolescents. But despite the risks, they continue to arrive. In the first three months of 2023, Brazil set a record for the entry of Venezuelans. In March alone, 17,471 migrants entered the country, according to the International Organization for Migration (IOM).</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Murillo Martins of the Public Defenders Unit, which works especially with Venezuelan children and adolescents in Pacaraima, says it is common for boys to arrive from mining areas in that country.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“They either say they were with their parents in the mines in Venezuela, or they say they were alone working there, and their story is mainly that they were coming in search of better conditions and work in Brazil,” he says.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>In 2018, Brazil launched Operation Welcome (<em>Operação Acolhida</em>, in Portuguese), a militarized humanitarian effort to handle the flow of Venezuelans on the northern border. In a January 2022 report by the R4V inter-agency coordination platform, which gathers data about Venezuelans served by Operation Acolhida, 39% of the people in 800 households surveyed were age 17 or younger. Of those households, 42% were in Roraima or neighboring Amazonas, and 25% of the household members between ages 15 and 17 were not attending school, which is a basic right under Brazilian law. In 15% of the households, there was at least one child or adolescent whose parents had stayed in Venezuela or lived in another Brazilian state.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Some minors arrive alone, without identity documents, and others arrive with adults who are not relatives. Vulnerable, they become easy prey for criminal groups, whose influence has spread from the prisons to the streets.</p>
<!-- /wp:paragraph -->

