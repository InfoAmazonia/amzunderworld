<!-- wp:paragraph -->
<p>Miguel, a charismatic man in his early 30s, crossed the border into Brazil in 2014 at the town of Pacaraima, some 200 kilometers (124 miles) north of Boa Vista. A year earlier, Venezuela’s economy had collapsed under the weight of political turmoil, widespread corruption and the devastating decline of its poorly managed oil sector, which had been the country's economic backbone. The situation worsened in 2017 when the United States imposed financial and economic sanctions, further isolating Venezuela's struggling economy, which was already grappling with hyperinflation.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>For many families, there is not much left but misery, which has caused more than 7 million Venezuelans, especially young people, to leave the country since 2014, according to the U.N. Refugee Agency. Almost 500,000 have migrated to Brazil.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>It’s common for entire Venezuelan families to sleep for days or weeks in the streets of Pacaraima while waiting to get the documents they need to enter Brazil legally. Some remain, cobbling makeshift shelters out of canvas and cardboard, because they lack funds to continue their journey or they do not want to stray far from their homeland’s border.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":164817,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
