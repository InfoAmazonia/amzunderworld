<p>“We work to get these children and adolescents a legal immigration status and identify situations of vulnerability, of risk,” so protective measures can be taken, public defender Martins says in an interview on the grounds of Operation Welcome in Pacaraima. Unaccompanied minors arriving there are taken to shelters specifically set up for those under age 18.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“Roraima is on a triple border” with Venezuela and Guyana, says Moacir Collini, one of the founders of Crescer, a project that offers young people between ages 15 and 24 from the outskirts of Boa Vista activities such as crafts, baking and sports.&nbsp; “Institutions need to have the same presence as criminals do, to give young people prospects for the future.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Beto*, a 20-year-old Brazilian who says his father and one brother were linked to the PCC and were murdered, participated in Crescer’s workshops in late 2022. Learning carpentry and baking gave him an alternative, he says.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“I saw my father dying, and then I saw my brother. And now I have my [1-year-old] son. And then I keep thinking about my life ahead, which path I am walking, which friendships. ... They’ve already called me — to drink, smoke, kill, steal, go to the mines,” he says. “I'm not going.”</p>
