<h1 class="wp-block-heading" id="h-family-breakdown-and-early-death"><strong><strong>FAMILY BREAKDOWN AND EARLY DEATH</strong></strong></h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>“First comes the family breakdown,” says Aminadabi dos Santos, coordinator of the Special Protection Department of Roraima’s Secretariat of Labor and Social Welfare. He has worked for 20 years with youth and adolescents in the region and ran a public shelter in Boa Vista for four years. In 2020, the shelter housed 30 residents, almost twice its capacity, mainly Venezuelan teens who had arrived in Brazil alone.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>At a similar shelter in Boa Vista, José* sits on a wooden bench.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“I like to crochet. When I'm standing still, my hands and feet are like this, moving,” he says, his left leg swinging up and down frenetically. Just 15 years old, José has spent nearly half his life between the streets of Ciudad Bolívar, in his native Venezuela, and public shelters in Roraima.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":164808,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/08/story7-ft1.jpeg" alt="" class="wp-image-164808"/><figcaption class="wp-element-caption">A teenage Venezuelan, who lives in a shelter after arriving in Brazil unaccompanied, holds two crochet pieces that he is unable to finish because of a lack of yarn. | Diane Sampaio</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>Before crossing the Brazilian border unaccompanied, he was in Venezuela’s southern mining region, waiting tables at a hot dog stand in a place known as Kilometer 88. He earned just enough to buy food, while seeing younger boys — some as young as 9 — already involved in drug trafficking. Afraid, he decided to leave.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“They liked me there and told me to stay, but I didn't want to and came here,” he says, sitting in the courtyard of the newly renovated shelter where he lives, almost 1,000 kilometers (621 miles) from home. He wears black denim shorts, a gray polo shirt that’s a size too big and worn rubber slippers. In his hands he holds two pieces of crochet in neon orange — his creations, he says, but unfinished. There is no yarn.&nbsp;&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>It was alone and at age 16 that Jesús Alisandro Samerón Pérez arrived in Pacaraima, telling the social welfare service at the border that he was looking for an opportunity in the country. In June 2019, his case was processed and he was given shelter in the town.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Four months later, he was found dead near a shelter for Venezuelans in Boa Vista. His body, with signs of torture, was wrapped in a garbage bag and left in a wheelbarrow. The executioners, at least eight Venezuelans and one Brazilian, were identified as local PCC members.&nbsp;&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>According to the police investigation, Jesús was killed for a drug debt of $200 <em>reais</em> (about $40). Four years after the murder — and despite media attention at the time — none of them is in prison. </p>
<!-- /wp:paragraph -->

