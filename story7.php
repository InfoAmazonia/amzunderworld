<h1 class="wp-block-heading" id="h-venezuelans-in-the-pcc"><strong><strong>VENEZUELANS IN THE PCC</strong></strong></h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Local authorities have worried for years about the growth of criminal groups like the PCC and the Red Command or Comando Vermelho (CV), which has its roots in Rio de Janeiro, and which expanded to Roraima beginning in 2013.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The situation has worsened in recent years. In 2016 and 2017, two massacres in the Monte Cristo Agricultural Penitentiary, the state’s largest prison, left 43 inmates dead. The riot came after a split between the PCC and CV that led to a series of prison massacres in Brazil, reflecting the way the groups have expanded through the country’s prison system. In 2018, a series of attacks on banks and public agencies in five cities in Roraima was further evidence of the PCC’s foothold.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>After the uprisings, the Monte Cristo Agricultural Penitentiary was under federal control until 2022, when it had a prison population of 1,800 men, 226 of them Venezuelans.</p>
