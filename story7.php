<p>According to data obtained by Amazon Underworld through the Access to Information Act, between 2020 and 2022, the Federal Police in Roraima conducted 19 operations against criminal groups in the state, 18 of them against members of the PCC and one against members of the CV.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The best known was Operation Triumphus in 2020, which identified a nucleus of Venezuelans in the PCC in Roraima. In August of that year, the state prosecutor’s office charged 19 foreigners between ages 24 and 46 with coordinating their organization’s internal disciplinary procedures, known as “criminal courts,” as well as managing drug sales points and storing weapons and ammunition.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Investigators in that case claimed to have found that at least 740 Venezuelans were members of the PCC, more than three times as many as are in the local prison system and equivalent to 40% of the 2,000 members that local authorities say the PCC currently has in the state, where the CV has around 500.</p>
