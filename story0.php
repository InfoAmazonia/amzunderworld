<!-- wp:paragraph {"className":"big"} -->
<p class="big">The Amazon, known as the largest rainforest in the world, also functions as a source and transit point for illegally extracted jungle resources and narcotics, linking nine countries together.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"big"} -->
<p class="big">As criminal economies expand in size, the levels of violence and deforestation worsen, and the Amazon and its communities face an existential threat.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Beneath the lush canopy of the world's most biodiverse ecosystem lies a darker reality, hidden behind the natural splendor. A labyrinth of thousands of rivers allows criminal groups to move freely while the dense forest cover enables them to evade detection.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The remoteness and lack of infrastructure cause a profound disconnect between Amazon communities and capital cities. Historically neglected by their governments, entire regions and populations are left isolated and excluded.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Porous and difficult to control, borderlands are marked by an obscure convergence of guerrilla movements, criminal startups and multinational organized crime.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Armed groups, drug traffickers and illegal miners encroach on or invade protected areas and Indigenous territories. In some places, park guards have been forced to abandon their posts.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Some Indigenous communities rent their land for drug crops or mining. Those who resist risk assassination. Despite efforts to protect the Amazon and its indigenous and traditional peoples, criminal groups have seized control of remote regions.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>This is the Amazon Underworld, where cycles of crime &amp; destruction are fueled by a multibillion-dollar illegal trade in drugs, gold and arms.</p>
<!-- /wp:paragraph -->

