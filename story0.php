<p>Country borders closed down during the global pandemic, but criminal enterprises know no international frontiers and have increased their control while governments limited their actions in the Amazon.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Remote border regions have transformed into havens for criminal networks, where they interact, engage in illicit trade, fuel conflicts, cross borders to evade law enforcement, and expand their illegal economies.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>One example is the triple border between Colombia and Peru, the two largest producers of cocaine globally, and Brazil, which serves as both a significant consumer market and a crucial transit area. In this region, the Amazon River acts as the starting point for cocaine shipments that are transported to port cities along Brazil's Atlantic Coast. From there, the drugs are distributed to markets in Europe, Africa, and the Middle East.</p>
<!-- /wp:paragraph -->

