<p>One Colombian crime boss says that country’s Amazon borderlands are a hotbed for organized crime. “This is a warehouse, a Persian market. You can get everything from a .38-caliber gun to a surface-to-air missile here in the Amazon.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>As the future of the Amazon is at stake, criminal groups launder their profits through land speculation and seemingly legal activities like cattle ranching. The process destroys&nbsp; Amazonian forests and ensnares Indigenous and other traditional communities in spiraling cycles of violence.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>In our project ‘Amazon Underworld’ we dive into crime dynamics in the Amazon and cover the social, cultural and environmental impacts of illicit economies that are changing the face of the jungle forever.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Through a cross-border approach and the application of qualitative and quantitative methods, we have worked with a collaborative network of journalists to map armed groups, criminal organizations and illicit economies of the Amazon borderlands in Bolivia, Brazil, Colombia, Ecuador, Peru and Venezuela.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"big"} -->
<p class="big">In 15 months of research in the field and behind our laptops, using satellite images and building new databases, a collective effort of 24 journalists from 9 countries narrates the Amazon Underworld from within through eight in-depth stories.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Amazon Underworld is a joint investigation of InfoAmazonia (Brazil), Armando.Info (Venezuela) and La Liga Contra el Silencio (Colombia). The work is carried out in collaboration with the Pulitzer Center’s Rainforest Investigations Network and financed by the Open Society Foundation and the U.K. Foreign, Commonwealth &amp; Development Office.</p>
