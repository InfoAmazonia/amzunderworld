<!-- wp:paragraph {"className":"big"} -->
<p class="big">By María Ramírez Cabello</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>It’s Friday night, and in the center of Ikabarú, a community in the PemónIndigenous territory in thesouthern Venezuelan Amazon, the preaching of a couple of Christian pastors brings back bad memories tolocal residents. The scene, the hustle and bustle, and the singsong voices are similar to those of the daysbefore the so-called Ikabarú massacre, an armed operation three years ago in which eight people werekilled.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The event—on the night of Friday, Nov. 22, 2019-—signaled the importance of the Indigenousterritory for criminals involved in illegal gold mining in this vast area of southern Venezuela andneighboring countries.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>A cocktail of armed groups—guerrillas, illegal gold miners and criminal gangs known as syndicates or“systems”—shares control of mining deposits in southern Venezuela, in the states of Amazonas,Bolivarand Delta Amacuro. They finance their operations with income from extortion and the trafficking ofminerals, drugs and weapons, and operate with virtually no resistance from the government in thebiodiverse and sparsely populated region.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading" id="h-map-5-2-armed-groups-present-in-the-states-of-amazonas-bolivar-and-delta-amacuro-venezuela-and-roraima-brazil">Map: 5.2 armed groups present in the states of Amazonas, Bolívar and Delta Amacuro (Venezuela)and Roraima (Brazil)</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Ikabarú is in a jungle area practically surrounded by armed groups operating in the north and east of the Venezuelan Amazon and by illegal gold miners, known as <em>garimpeiros</em>, who operate to the west in Brazil. Much is at risk. Besides being part of the Amazon, this area of the Venezuelan jungle feeds the Caroní River, Venezuela's second most important river and the country's main source of electricity.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>It’s no coincidence that this lawless mining area overlaps two environmentally protected areas, one of which is a strategically important water source.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>In the village, there is no need to ask what happened the day of the Ikabarú massacre. The story emerges spontaneously, usually as an explanation for the slow pace of the days here, which pass in silence, with few visitors and with the nostalgic air of those who have lost something valuable.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“There were people who left. There was trauma. Now they are returning,” one resident says, standing just a few meters from the place where the shooting began.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>That night, at around 7 p.m., a dozen armed men, hooded and dressed in black, shot at a group of people in front of a commercial establishment, shouting, “Your leadership is over, Cristóbal; this is El Ciego’s gang.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Cristóbal Ruiz Barrios, whose body appeared several days later not far from Ikabarú, was a miner in a deposit known as La Caraota, and those who were looking for him intended to replace him, as has happened with other mining claims in southern Venezuela’s rich gold and diamond deposits.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":163169,"width":810,"height":540,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full is-resized border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story2-ft4.jpeg" alt="" class="wp-image-163169" width="810" height="540"/><figcaption class="wp-element-caption"><strong>Dozens of miners work in this mine on the banks of the Uaiparú river, a tributary of the Ikabarú, leaving a gash in the earth</strong> | María Ramírez Cabello</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>El Ciego (Spanish for “the blind man”), the alleged mastermind of the plan, heads one of at least seven active criminal groups that control mining areas in the Venezuelan jungle, where they have operated with the tacit knowledge of state forces for more than a decade.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>But because of the peculiarity of the event, local leaders — Indigenous and non-Indigenous — attributed the massacre to state security forces. The objective, they say, was to justify further militarization of the Pemón territory and absolute control of the mineral wealth.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>That same year, a Venezuelan Army convoy on its way to the Brazilian border fired on the Kumarakapay Indigenous community, killing three Pemón and leaving dozens wounded, a clash that led to a military advance amid a decade of retreats in the struggle for control among guerrillas, <em>garimpeiros</em> and even military forces that share the territory.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“It’s part of the government’s strategy, which reached its peak in 2019. They sponsor these armed groups and create a situation to justify militarization,” says one Indigenous leader, who requested anonymity for security reasons. Abuses by the military and the invasion of Indigenous territories by armed groups intensified around a decade ago, she adds.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Amid this upheaval, Ikabarú seems like an island, one of the last places that has not been taken over. Although the days there seem to pass tranquilly, the threat of incursion by criminal groups and new actors seeking a share of the gold and diamond wealth remains latent.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“They are entering the communities little by little, like a peaceful invasion,” people in Ikabarú warn.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Although there is no active armed organization as there is in the country’s other mining districts, the area is coveted by the so-called “syndicates” or “systems” that operate in neighboring municipalities. It is also eyed by <em>garimpeiros</em> just 12 kilometers (7.5 miles) south, in Brazil, where they invaded the ancestral lands of the Yanomami people with support from criminal groups.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>A third group — which is not new, but which has gained renewed strength — may also have its sights set on the region: Guyanese miners, who operate huge platforms where they scoop up riverbeds in the upper basin of the Caroní River.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":163163,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story2-ft6.jpeg" alt="" class="wp-image-163163"/><figcaption class="wp-element-caption">Ikabarú is a community of Indigenous and non-Indigenous people in the municipality of Gran Sabana, in southern Venezuela. Its gold and diamond deposits are a magnet for miners and armed groups. | María Ramírez Cabello</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>Since the mid-2000s, state and non-state armed groups have been violently competing for control of mining areas in southern Venezuela. The greatest pressure has been concentrated in Bolívar, the largest state in the Venezuelan Amazon, where the U.N. Human Rights Council has documented massacres, disappearances and human rights violations.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Violence and mining expansion have peaked in the years of lowest oil production, the main source of income for this nation, which has the world’s largest proven crude oil reserves. In 2016, the government of President Nicolás Maduro created the Orinoco Mining Arc, saying it would “reorganize” mining in the south. Instead, however, illegal mining has expanded rapidly in the area, facilitated by armed groups that wield criminal authority.</p>
<!-- /wp:paragraph -->

