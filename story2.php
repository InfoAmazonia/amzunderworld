<p>Between 2004 and 2010, Venezuela registered an average annual production of about 18,000 carats of the gem under the Kimberley Process, a voluntary agreement in which 82 countries set minimum standards for the supply of conflict-free diamonds. Venezuela pulled out of the agreement in 2008 because it was impossible to monitor the origin of the stones, but returned to the process in 2016.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>In 2020, Venezuela reported production of 794 carats on the international certification system portal, but informality has gained ground, mining is illegal and much of the production is not reflected in official statistics. As part of the common agreement, participating countries commit to implementing internal controls and publishing the figures of their annual diamond trade, as well as to transparency in production and trade data. Venezuela has filed no reports for 2021 and 2022.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>But although mining is illegal, the state-run Venezuelan Mining Corporation (CVM) reached an agreement with some Indigenous communities to supply materials. A 230-liter (60.7-gallon) can of fuel sold by CVM, for example, costs 6.5 grams of gold.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The fragility of the situation is evident. Even at the expense of their territory, the Pemón people in this corner of the jungle have proposed legalizing mining so they can consign gold to the Maduro government in a country fragmented by the actions of criminal groups. They want to open the door to legal mining.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“We no longer want to hide or pay extortion fees, but pay what is due,” says González, the captain general. For now, the potential hazards that would be exacerbated by increased mining do not seem to worry him much.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“This is our territory,” he says, “and we are going to keep it.”</p>
