<!-- /wp:cover -->

<!-- wp:paragraph -->
<p>Jeremías* believes she’ll take over the business eventually. It’s an ideology, he says. Just as he followed in his father’s footsteps, she will follow in his.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>His father transported cocaine base paste. Jeremías manufactures it, and his daughter helps him by deflecting suspicion and concealing money.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“Once, when we were returning home after delivering packages, I had to bring more than 100,000 reais in cash,” he recalls, referring to Brazil’s currency.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“Almost all of it was to pay the coca pickers and buy supplies for the next season. She helped me with that, too — she brought it, attached to her body. Who’s going to say anything?” He smiles, though whether that’s from nerves or cynicism is hard to tell.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Jeremías and others like him in Mariscal Ramón Castilla are the first links in a chain of drug production that stretches from fields of coca, the plant whose leaves provide the active ingredient in cocaine, through clandestine laboratories nearby, down the Amazon River to the triple border, then by river or air to cities on Brazil’s Atlantic coast and on to consumers in Europe.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":160573,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story2-img1.jpg" alt="" class="wp-image-160573"/><figcaption class="wp-element-caption">Much of the illicit drug supply produced in Peru is shipped through Tabatinga, a Brazilian town on the Amazon River, on the triple frontier shared by Peru, Brazil and Colombia | Bram Ebus</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>Known as a patrón, a local drug-trafficking boss, Jeremías has his own coca crops and oversees fields on other people’s farms. He coordinates the processing of coca leaves into cocaine base paste, which will later be refined into cocaine hydrochloride. He and the people around him — the ones who spend the day picking coca leaves in the tropical heat, and those who mix the leaves with toxic chemicals to produce the paste — are also among the lowest-paid workers in the illegal drug industry.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Besides growing coca and manufacturing base paste, he used to transport his wares to the triple border. Then outsourcing came to the drug trade in this part of the Amazon. Now middlemen from Colombia have taken over the logistics, picking up the drugs from the farm and saving him the long and possibly perilous journey to Tabatinga.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Thirty-odd years old, Jeremías is a family man with a military bearing who rarely uses the familiar form of addressing people in Spanish. He is not a man of few words, but his deliberate way of speaking gives a listener that sense. If he could have chosen, he says, he would have been a soldier, because he likes guns.</p>
<!-- /wp:paragraph -->

