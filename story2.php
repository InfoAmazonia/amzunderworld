<!-- wp:paragraph -->
<p>The first “missile” beyond the platform under construction is operated by a group of Guyanese miners whose camp is just a few kilometers away.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“The captaincy has full knowledge of these Guyanese and Brazilian dredges. They are the ones with the biggest equipment,” says a Pemón resident of the community of Playa Blanca, who asked to remain anonymous.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Thin, amber-colored tributaries of the Ikabarú join the thick, swirling river, highlighting the ecological damage to the waterway, whose banks are home to at least a dozen Indigenous communities. On shore and in ponds, toxic mercury is used to amalgamate the gold, extracting the particles from sand and sediment. The highly volatile heavy metal then returns to the air, soil or water. In the water, it is transformed into methyl mercury and accumulates in fish tissue, an effect that magnifies as it goes up the food chain.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“We know this is illegal and that we are damaging the environment,” says José G., a member of a communal council that is being created in the port of Los Caribes, the supply point for food and other items used in the mines and on the rafts. This non-Indigenous organization, protected by the Law of Communal Councils, is another factor in conflict in the territory. The Indigenous captaincy maintains that the organization wants to impose its own interpretation of the law that created communal councils as participatory bodies for facilitating the implementation and oversight of public policies and community development projects.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>They agree on one thing, though. Like the Indigenous people, José G. justifies the rafts’ presence, saying their environmental impact is minimal. He and his team, he says, control security and access to the mines. In the absence of the State, they have taken the law into their own hands, drawing up improvised “expulsion notices” for anyone who enters the mines to “misbehave” by committing abuses or causing violence.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Both land and raft miners extract gold and diamonds. According to a 2021 study in a journal of the Ministry of Science and Technology, Ikabarú is the territory in the state of Bolívar with the greatest potential for diamond production. The authors write that most of the diamonds in Ikabarú are of high value because they are eight-sided crystals with cut percentages — a reference to the number of the facets, which give the diamond its brilliance — greater than 60%.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Miners in Ikabarú talk little about diamond mining. Ramón S., a 31-year-old miner waiting in the port of Los Caribes for a boat to take him to the nearest mine, takes a small plastic container out of his pocket and empties tiny, perfectly shaped diamonds into his hand.</p>
