<h1 class="wp-block-heading" id="h-coca-expansion-outstrips-eradication">COCA EXPANSION OUTSTRIPS ERADICATION</h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Mariscal Ramón Castilla has long been a land of original peoples, where the Indigenous Ticuna, Yagua and Bora peoples still predominate. Until the 1990s, it was just a transit point for drugs heading for the triple border. The source of the coveted white powder was far to the west, on the slopes of the Andes Mountains, far from this seasonally flooded, low-lying plain. The Amazonian lowlands were not suitable for agriculture, but they had enormous strategic potential because of their location.</p>
