<!-- wp:paragraph -->
<p>Jeremías remembers the date of his river trip: It was July 20, 2021, and on the triple border shared by Peru, Brazil and Colombia on the Amazon River, a three-nation festival was under way. He was worried, because the festivities meant tighter police and military controls at checkpoints along the river. And he was just the kind of person the police would be looking for.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>But he had to go. That was the day he was due to transport a shipment of cocaine base paste, a cocaine precursor, that he had produced at his farm. Buyers were waiting in the Brazilian border town of Tabatinga, and he couldn’t fail to show up.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Then he had an idea. If he traveled with his 9-year-old daughter, he’d be less likely to raise suspicions. What could be more ordinary than a father taking his daughter to the festival? And she was always ready for adventure.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“She said, ‘Come on, let’s go!’ She wanted to take the risk. Either you get caught or you get through to your destination. That was the concern about taking her along. But you have to stay calm. She already knows that,” he recalls, sitting on a rough wooden bench outside his house in one of many Indigenous communities in Mariscal Ramón Castilla, a province in the far northeast corner of Peru that borders Colombia and Brazil.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>He adds matter-of-factly, “Here in town, DIRANDRO [Peru’s anti-narcotics police] has almost caught us</p>
<!-- /wp:paragraph -->

<!-- wp:cover {"url":"https://infoamazonia.org/wp-content/uploads/2023/07/bg-green.jpg","id":160150,"dimRatio":0,"align":"full"} -->
<div class="wp-block-cover alignfull"><span aria-hidden="true" class="wp-block-cover__background has-background-dim-0 has-background-dim"></span><img class="wp-block-cover__image-background wp-image-160150" alt="" src="https://infoamazonia.org/wp-content/uploads/2023/07/bg-green.jpg" data-object-fit="cover"/><div class="wp-block-cover__inner-container"><!-- wp:heading {"textAlign":"center","level":1,"textColor":"white"} -->
