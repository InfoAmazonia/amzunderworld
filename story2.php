<!-- wp:paragraph -->
<p>Official recognition of the Pemón people’s territory has not prevented incursions by armed outsiders. In 2016, Indigenous leaders set up two checkpoints on the road to Ikabarú to control access. In 2017, the Indigenous rights organization Kapé Kapé reported that a group of 70 armed people “of various nationalities” had kidnapped the inhabitants of Hachaken, another Pemón community, for four days. According to the report, they intended to take control of gold mining there — the same motive that was behind the 2019 Ikabarú massacre</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>But Ikabarú has not been taken.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“Between July 2016 and March 2017, more than 8,000 miners entered Ikabarú, some of them even on foot,” recalls Lisa Henrito, captain, or leader, of the Maurak Indigenous community. The situation at the Kimiyo mine in Hachaken forced the Indigenous territorial guard to evict 648 of those miners.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“In that group, we detected seven subjects linked to ‘El Ciego’ of La Paragua. That group was already raising its head. They even tried to kidnap two territorial guards,” she says. It was an attempt to seize that island in the state of Bolívar.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“The intention behind that massacre was to take over the mines and bring in their people, but we know where these intentions come from — from the government itself. Those are blatant actions,” says Juan Gabriel González.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>González is&nbsp; general captain of Sector VII of the Pemón people, one of eight geographical areas into which the Indigenous communities of Venezuela’s Gran Sabana are organized. Each community and each sector has a leader, called a captain,</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Six months after the massacre, in 2020, members of the criminal group contacted him. One said he was El Ciego and he wanted to talk to González.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“They started writing to us that they wanted to send people in to control the mines, but we rejected that in a statement issued with the communal councils,” the Pemón leader says. “We will not accept those people.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>When miners began flooding into Ikabarú, the indigenous leadership, or captaincy, took stronger measures to control access. Even now, anyone wanting to enter the area must request authorization from the general captaincy in Santa Elena de Uairén. Travelers going to Ikabarú generally must show the permit at three checkpoints set up by the Indigenous territorial guard, an internal security structure created in 2001 to resolve domestic problems in the community, but which over time became a form of self-defense against invasion by outsiders. The Pemón people who participate wear black and act as sentries, but do not carry firearms.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>That control, however, has not stopped the threats and the influx of miners. In a September 2022 report, the U.N. Independent International Fact-Finding Mission on Venezuela noted that “given their strategic position and gold wealth, the indigenous territories in Gran Sabana and other neighboring municipalities have been a focus of interest for both the State and armed criminal groups. The indigenous population in these areas has resisted these interests, which has led to conflicts and violent clashes.”</p>
<!-- /wp:paragraph -->

