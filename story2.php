<!-- wp:heading {"level":1} -->
<h1 class="wp-block-heading" id="h-missiles-on-the-ikabaru-river">‘MISSILES’ ON THE IKABARÚ RIVER</h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>As <em>garimpeiros’</em> increasingly set their sights on this region of Venezuela, one concrete sign of their interest is the arrival of huge rafts carrying dredging equipment, a phenomenon imported from Brazil.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>A trip along the Ikabarú River reveals only a couple of active open-pit mines and more than a dozen abandoned deposits. In satellite images, these mining camps appear to be active. But people familiar with the area say that those operations, where diamond-washing machinery still sits on the river bank, were paralyzed during Plan Caura, a 2010 military operation aimed at eradicating illegal mining.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Nevertheless, in the meandering river, bordered by thick forests, where macaws fly across the sky, the turbidity of the water is not an encouraging sign.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>On the river bank, a couple of men assemble huge, green metal pieces that arrived a few days earlier, transported in five trucks along the dirt road that leads to Ikabarú and the port of Los Caribes. The passing of the convoy one morning in mid-February did not go unnoticed. It’s not unusual to see heavy vehicles loaded with food and gasoline, but the huge platforms were the talk of the town. And they’re not the only ones. Beyond the docks, three other giant rafts operate continuously.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Known as “missiles,” these enormous dredges work illegally day and night. They are so wide that besides holding heavy equipment that stirs up sediment on the riverbed and sucks it through pipes that connect to a large sieve, where the gold and diamonds are washed, they also have space for three to five operators to move around easily and have a rest area.</p>
<!-- /wp:paragraph -->

