<!-- wp:heading {"level":1} -->
<h1 class="wp-block-heading" id="h-gold-and-diamonds-a-magnet-for-miners">GOLD AND DIAMONDS A MAGNET FOR MINERS</h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Ikabarú is at the end of a nearly impassable dirt road that resembles the rocky bed of a river that has dried up. It is in the Pemón Indigenous territory in the municipality of Gran Sabana, in the southern state of Bolívar, about eight hours from Santa Elena de Uairén, the largest town in the area, and 12 kilometers (7.5 miles) from the Brazilian border.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Along the roadside, a visitor approaching Ikabarú sees Indigenous people with metal detectors waiting for the sound that will alert them to the presence of precious metal. Renting the equipment, for a tenth of a gram of gold per day, is one of the new businesses. The prospectors search among innumerable mounds of earth extracted from old mining claims, mainly in the 1990s, when the government granted mining concessions. It is literally a mine field, where the once-crystalline waters of streams like the Chaveru are now muddy with sediment churned up by miners.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Mining is clearly the central livelihood here. In town, where the population is mainly non-Indigenous, signs advertising sweets and bread are mixed with others offering to purchase gold and diamonds, and still others hawking gasoline, diesel fuel and electric generators. In the daytime, there is no electricity and the town falls into silence. Countless houses seem abandoned or have been put up for sale. Of 10 restaurants active before the massacre, only one remains.</p>
<!-- /wp:paragraph -->


