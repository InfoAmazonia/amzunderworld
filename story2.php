<!-- wp:heading {"level":1} -->
<h1 class="wp-block-heading" id="h-brazilian-miners-cross-the-border">BRAZILIAN MINERS CROSS THE BORDER</h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Venezuelans are not the only ones who covet the Pemón territory. Brazil and Venezuela share a 2,199-kilometer (1,366-mile) border. At the border crossing that connects the Venezuelan town of Santa Elena de Uairén with the Brazilian town of Pacaraima, tens of thousands of Venezuelan migrants have passed through tents set up by the U.N. Refugee Agency (UNHCR). But informal crossings elsewhere in the jungle may see a flow in the opposite direction.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":163178,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story2-ft1.jpeg" alt="" class="wp-image-163178"/><figcaption class="wp-element-caption">Venezuelan migrants seeking to escape their country’s political and economic upheaval cross the border to the Brazilian town of Pacaraima. | Diane Sampaio</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>"Ikabarú can be reached by land, by air and by trails from Brazil," says Luis Rodríguez, a 33-year-old Venezuelan miner who came to try his luck along the banks of the Ikabarú River less than a year ago, attracted by the reputed purity of the gold in these deposits.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The presence of Brazilians on Venezuelan soil is not unusual — they have been mining here for decades.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“The Brazilians taught the Venezuelan Indigenous how to drill,” said one local resident, referring to the prospecting technique for analyzing soil and locating mineral deposits.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":163160,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story2-ft7.jpeg" alt="" class="wp-image-163160"/><figcaption class="wp-element-caption">A prospector searches for gold in an area mined years ago. The rental of metal detectors is a booming business in this area of the Gran Sabana municipality. | María Ramírez Cabello</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>But Brazilian President Lula da Silva’s pledge to put an end to illegal mining in Yanomami territory in that country could lead more Brazilian miners to cross the border, posing a threat to Indigenous peoples in Bolívar and across the Venezuelan Amazon. This region has seen the greatest violence associated with illegal mining, including the Haximu massacre in 1993, in which 16 Yanomami people were brutally murdered by Brazilian miners.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>On the banks of the Ikabarú and Uaiparú rivers, earth-moving equipment rumbles as it fractures the Earth’s crust to create open-pit mines. These powerful pieces of equipment are owned by Brazilians. These points are more than a two-hour drive&nbsp; from Ikabarú over a cobblestone road that makes access nearly impossible. The road leads to the river port of Los Caribes. From there, the illegal deposits can be reached by motorboat.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>González, the Pemón leader, confirms that Brazilians have been working in the area for more than 30 years under the rules of the Indigenous captaincy. But with the military eviction from Brazil, they have begun to enter Ikabarú. Miners isay the routes they use include old trails in Sierra Beleza or La Leoncia.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“They have wanted to enter Ikabarú, but we are rejecting them,” González says. “They have been displaced and evicted, but we do not allow them to enter our territory.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>According to González, the <em>garimpeiros</em> have tried to gain entry by offering to finance mining equipment for residents of. “We already know what mechanisms they want to use to enter the area. We have closed the permits to enter,” he says, adding, “We’re mounting surveillance. We are alert.”</p>
<!-- /wp:paragraph -->

