<h1 class="wp-block-heading has-text-align-center has-white-color has-text-color" id="h-static-map-drug-trafficking-routes-laura">STATIC MAP: DRUG TRAFFICKING ROUTES LAURA</h1>
<!-- /wp:heading --></div></div>
<!-- /wp:cover -->

<!-- wp:paragraph -->
<p>Besides Tabatinga in Brazil, there are other important points where buyers stockpile drugs, including the Colombian towns of Leticia, which adjoins Tabatinga on the triple border, and Puerto Nariño, slightly downstream from Caballococha and on the other side of the Amazon River. In Peru, there is Santa Rosa, on an island opposite Tabatinga and Leticia, and the Indigenous community of Bellavista Callarú, about midway between Caballococha and the border.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Bellavista is increasingly controlled by drug-trafficking groups. It is a community on the bank of the Callarú River, which is only navigable in the rainy season, and where soccer is a favorite activity of both men and women. Its plaza — an overgrown area with some stairs and arches — offers a view of spectacular Amazonian sunsets, a beauty that contrasts with the sight of armed outsiders drinking beer until they pass out.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":160644,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story2-img4.jpg" alt="" class="wp-image-160644" title="border-full"/><figcaption class="wp-element-caption">Coca leaf pickers work 12- to 15-hour days in the tropical heat, earning about $0.25 per kilo (2.2 pounds) of coca harvested | Alex Rufino</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>Strangers who are allowed to enter are followed and harassed with questions meant to intimidate. On March 21, a young victim of sexual exploitation was murdered and no one ever spoke up, one local resident says. People know who killed her, but no one will say anything, the person added, because “that’s what happens when you don’t play straight with a patrón.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Coca is the only law. On June 15, a familiar announcement was broadcast over the community’s loudspeakers in Spanish and Ticuna, the local Indigenous language: “This is a notice to anyone who wants to go pick coca in Lucho’s field.”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Scenes like that are not unusual. Neither is the exploitation of minors, according to one curaca, or leader, of an Indigenous community on the Colombian side of the river, who says he receives constant threats because of his efforts to warn community members about the risks of letting young people go to work in the coca fields in Peru.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Men are taken to the fields and women to the bars, the leader says. “Don’t let the girls go — they take them, have sex with them and raffle them off,” he warns parents who have daughters over age 11.</p>
<!-- /wp:paragraph -->

