<!-- wp:paragraph -->
<p>One of those points is Caballococha, the largest town in Mariscal Ramón Castillo, which boasts a few streets of shops and an unfinished port dominated by a statue of a rearing white horse. Sitting in the plaza on a Sunday morning, Jeremías is impatient to begin the next harvest. He plans to return in the afternoon to the community where he lives with his family, and where he has operated his business for the past 14 years. It’s a four-hour trip by boat, and he must leave soon to arrive before nightfall.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>In recent months, Jeremías has diversified his business, and not by chance. Besides the soft drinks and beer that he sold six months ago, he also sells gasoline, which is used not only to power boats but to extract the cocaine alkaloid from the coca leaf to make the cocaine base paste that is later refined into cocaine.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Jeremías also has a new foreman, a good-natured, 51-year-old Colombian who says he left his country because the government there wouldn’t let him grow coca in peace. During the harvest, he will work side by side with a squad of raspachines, or leaf pickers, who will be paid between $0.21 and $0.27 per kilo (2.2 pounds) of coca harvested. Working up to 15 hours in a day, a harvester can pick as much as 150 kilos (330 pounds). Many have hands so callused that they no longer feel pain from the work.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Carmelo*, an 18-year-old Colombian, began harvesting coca when he was 16. He didn’t finish elementary school and expresses no expectations about his future. He doesn’t know what he would have liked to have been or done, let alone that he still has time to decide. What he does know is how much the blisters on his fingers hurt after picking so many sacks of coca leaves, and what it feels like to fall ill from exhaustion or an insect bite.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>He also knows that he might have ended up in jail at least once, but he managed to slip away from the anti-drug police. “They made us run — they attacked the laboratories. I escaped. They beat us up and sent us to Iquitos.,” he recounts anecdotally.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":160567,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story2-img3.jpg" alt="" class="wp-image-160567" title="full-border"/><figcaption class="wp-element-caption">Coca fields are at a distance from the community, along a muddy trail through undergrowth | Pamela Huerta</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>The path to Jeremías’ field is muddy, with scattered vegetation because the forest has been cleared to expand the illicit crops. Without tree cover, the temperature is hellish. Jeremías sports a synthetic soccer jersey with the logo of the Argentinian team Boca Juniors and the number 17, worn by Peruvian player Luis Advíncula. Like most Amazonian farmers, he always has a machete in his hand.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Along the way, he points out the laboratory where he processes the leaves and laments that this year he can’t hire a chemist to take charge of making the base paste. He doesn’t mean a professional chemist, necessarily, but one who has learned the trade from experience. This year, he says, he can’t afford to pay one because the increase in fertilizer prices has blown his budget.</p>
