<!-- wp:heading {"level":1} -->
<h1 class="wp-block-heading" id="h-streamlining-the-business">STREAMLINING THE BUSINESS</h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Jeremías, his foreman and Mario are small but important gears that keep the drug-trafficking machinery running smoothly. The same is true of the farmers who grow the crop, the workers who trample the leaves with toxic chemicals to make base paste, the cooks who prepare meals for the coca harvesters, the “backpackers” who carry heavy loads of drugs on foot across borders, and the other players in this perverse and illegal business. In this part of Peru, the outsourcing of services makes it difficult to figure out who’s in charge. The family clans, kingpins and drug cartels that once controlled the entire chain have become obsolete.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Diego Quintero Martínez, UNODC coordinator for security and emerging crimes, says the illicit drug trade has adopted a business model that makes the crime of drug trafficking increasingly difficult to tackle.</p>
<!-- /wp:paragraph -->

<!-- wp:cover {"customOverlayColor":"#d4c6ad","isDark":false,"align":"full","className":"tripa"} -->
<div class="wp-block-cover alignfull is-light tripa"><span aria-hidden="true" class="wp-block-cover__background has-background-dim-100 has-background-dim" style="background-color:#d4c6ad"></span><div class="wp-block-cover__inner-container"><!-- wp:quote {"className":"main-content"} -->
<blockquote class="wp-block-quote main-content"><!-- wp:paragraph -->
<p>“At first there was a very linear structure, where the person who was the head of the cartel controlled the entire process,” Quintero says. “But nowadays we see that there are subsystems. In Colombia, we’ve identified this kind of outsourcing [seen in Peru] as drug trafficking subsystems.”&nbsp;</p>
<!-- /wp:paragraph --></blockquote>
<!-- /wp:quote -->

<!-- wp:spacer {"height":"16px"} -->
<div style="height:16px" aria-hidden="true" class="wp-block-spacer"></div>
<!-- /wp:spacer --></div></div>
<!-- /wp:cover -->

<!-- wp:paragraph -->
<p>This is reflected in recent events. When the Familia do Norte lost control in Tabatinga, Os Crias — literally its “kids” — emerged. When Mexican drug boss Joaquín “El Chapo” Guzmán was captured, the “Chapitos,” his successors, appeared. The word “indispensable” no longer has any meaning in the drug underworld.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":160561,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story2-img6.jpg" alt="" class="wp-image-160561"/><figcaption class="wp-element-caption">The sheer number of rivers and streams in the Amazon Basin, combined with the remoteness, difficult access and cost of boat travel, makes it virtually impossible for countries to control illegal activities on their borders | Bram Ebus</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>The drug trade has a long history of adapting to — or staying a step ahead of — socioeconomic changes. It innovates. That may be one reason why efforts to eradicate it are largely unsuccessful. In the province of Mariscal Ramón Castilla, DEVIDA, Peru’s anti-drug agency, has been working on alternative development projects since 2014. So far, however, efforts to get farmers to switch from coca to other crops have not borne fruit.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Jeremías thinks those efforts are useless, because crops like cacao, from which chocolate is made, aren’t profitable.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“They try,” he says, “but there’s no market, and transportation costs make everything expensive.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Jeremías knows that his work supports a multibillion-dollar trade of which he gets only crumbs. If he could earn the same amount or more doing other work in Peru’s Amazonian lowlands, he says, he’d give up the illicit drug trade.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“But the way Peru’s borders are, they’ve abandoned us,” he says of the government. “Forget it!”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>He’s thinking about going into politics in his district in the future. He says he’d like to help his people, but it’s also the only option in Mariscal Ramón Castilla that could equal or exceed his income as a patrón. For the time being, though, he doesn’t see much of a choice — he’ll continue being a relatively poor drug drug boss, like so many others in the first link of the trafficking chain.</p>
