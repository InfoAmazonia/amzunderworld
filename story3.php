<!-- wp:paragraph -->
<p>The knowledge that drug traffickers in Colombia have acquired over the years is sought-after in Peru, says Col. Carlos Urquijo Gómez, second commander of Colombia’s Jungle Brigade No. 26 in Leticia.</p>
<!-- /wp:paragraph -->

<!-- wp:cover {"customOverlayColor":"#d4c6ad","isDark":false,"align":"full","className":"tripa"} -->
<div class="wp-block-cover alignfull is-light tripa"><span aria-hidden="true" class="wp-block-cover__background has-background-dim-100 has-background-dim" style="background-color:#d4c6ad"></span><div class="wp-block-cover__inner-container"><!-- wp:quote {"className":"main-content"} -->
<blockquote class="wp-block-quote main-content"><!-- wp:paragraph -->
<p>“They’ve taken Colombians [to Peru] because they know the entire process,” he says. “They’re specialists, and they’re sought out for that, as well as for their experience in [cocaine] laboratories.”</p>
<!-- /wp:paragraph --></blockquote>
<!-- /wp:quote -->

<!-- wp:spacer {"height":"16px"} -->
<div style="height:16px" aria-hidden="true" class="wp-block-spacer"></div>
<!-- /wp:spacer --></div></div>
<!-- /wp:cover -->

<!-- wp:paragraph -->
<p>Mario* is from Meta, Colombia, an area of constant conflict between guerrillas and paramilitaries. If you get involved in this work, he says, it’s because you want easy money. He gave it up for that reason, and also out of fear, because he found out how they’d killed his best friend. Before the pandemic, he shuttled drugs from Puerto Nariño to Tabatinga, the first stage of a route that leads to the Atlantic coast and then to foreign markets. He smuggled base paste and always carried a revolver.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“I transported drugs hidden in cassava or bananas, or sometimes in jerry cans of fuel,” Mario says, eyeing an armed soldier who enters the shop where he is talking with a visitor. He carried small amounts, between 10 and 20 kilos (22 to 44 pounds), which could be worth around US$200 to him once he’d deducted travel costs. He was always afraid, especially of Brazil’s Federal Police. Nevertheless, he says, “The trade there moves freely.”</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":160564,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story2-img5.jpg" alt="" class="wp-image-160564"/><figcaption class="wp-element-caption">Colombian soldiers patrol the river near Leticia, on the triple border shared by Colombia, Brazil and Peru | Bram Ebus</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>In Tabatinga, a kilo of cocaine base paste can cost as much as US$1,000 and a kilo of cocaine between US$2,500 and US$3,000. It all depends on the quality of the product, security conditions and a series of factors based on the “here and now.” From Tabatinga, the drugs go to Manaus, Brazil, the largest city in the Amazon and the main stockpiling point for that country’s criminal organizations, which handle domestic distribution and shipment to coastal ports.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>These dynamics make the area around the triple border extremely violent, with an exponential increase in murder for hire, human trafficking and general criminal activity. Colombian police statistics show a sharp increase in murders in Leticia, a town of some 33,000 people, from seven in 2020 to 27 in 2022.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The 2023 World Drug Report by the U.N. Office on Drugs and Crime (UNODC) continues to show Brazil as the largest consumer of drugs in South America. The country’s Atlantic ports are also a gateway for exporting cocaine to Europe, eastern Asia and the southern part of Africa. In those markets, a kilo of cocaine can cost as much as US$80,000.</p>
<!-- /wp:paragraph -->

