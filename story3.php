<h1 class="wp-block-heading" id="h-don-t-ask-don-t-tell">DON'T ASK, DON'T TELL</h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Jeremías knows the drug he produces will end up in Brazil. Until six months ago, he transported it himself, along with his youngest daughter. Now, though, buyers arrive to pick it up at his house. Most of them are Colombians, but they may be financed by Peruvians or Brazilians. As long as he gets paid in cash, he doesn’t ask questions. “Once I sold to a Mexican, but he never came back. I think they killed him,” he says matter-of-factly.</p>
