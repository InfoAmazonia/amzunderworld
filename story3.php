<h1 class="wp-block-heading" id="h-the-poorest-links-in-the-drug-chain">THE POOREST LINKS IN THE DRUG CHAIN</h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Jeremías has more than a dozen hectares (about half a square mile) of coca crops of his own in Mariscal Ramón Castillo, rents a few more, oversees the harvest in fields belonging to “friends” and buys some coca leaves from other farmers. All goes to producing base paste in his laboratory. That degree of control over production makes him a trafficking boss, although it seems an ostentatious title for a man who has only managed to build his house little by little, over four years.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>In the drug production chain, the patrón is the person who provides base paste or cocaine to middlemen at a fraction of the price the drug will eventually fetch on the streets of a city in the United States or Europe. Local bosses like Jeremías are at the mercy of the market, and in the economic underworld, that market is not self-regulating. That makes the Peruvian patrones and their hired hands the poorest links in the drug trafficking chain.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Prices are set by buyers, who on the triple border usually belong to the criminal organization that dominates the trade in Tabatinga. Since 2020, according to the Peruvian anti-drug police, a group known as “The Kids”—Os Crias in Portuguese, or Las Crías in Spanish—have been in control there.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The rise of Os Crias was the outcome of a bloody dispute for territorial control of the triple border among the Comando Vermelho (CV), Familia do Norte (FdN) and Primeiro Comando da Capital, three of Brazil’s main criminal organizations. Unwilling to share the drug trade in that area, they weakened each other. Dissidents from the three groups came together and took control, setting aside their old loyalties and forming a criminal consortium catering to the highest bidder. For the moment, at least, they have cornered the market on the drugs that arrive from collection points in the province of Mariscal Ramón Castilla.</p>
<!-- /wp:paragraph -->

