<p>Those figures contrast sharply with the area of coca uprooted by the Special Project for Control and Reduction of Illegal Crops in the Upper Huallaga (CORAH, for its Spanish initials), the agency responsible for eradicating coca crops in Peru. According to official figures, the last time operations were conducted to eradicate illicit crops in Mariscal Ramón Castillo was in 2019, when 7,784 hectares (30 square miles) of plants were uprooted. That effort focused on the province of Pebas, which borders Mariscal Ramón Castillo upriver from the triple border, and which can only be reached by river.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>So far this year, government officials report nearly 5,000 hectares (19.3 square miles) of illicit crops eradicated, but only in the regions of Ucayali, Huánaco and San Martín. In December 2022, CORAH set up a base of operations in Mariscal Ramón Castillo, raising both expectations and fear in local residents. As of June, however, the base was unoccupied. The Interior Ministry did not respond to repeated requests for comment.</p>
