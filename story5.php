<p><strong>By Pamela Huerta and Iván Brehaut</strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Four prisoners just over five feet tall, very young and agile as acrobats, are pitching a makeshift roof made of black sacks in the main patio of the prison in Pucallpa, a city of some 325,000 people that is the capital of Peru’s eastern Amazonian region of Ucayali. They stretch, twist, lift, hang and tie as though the movements were choreographed, creating an area of shade for a visitor.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Beneath the improvised tarpaulin, they arrange a table and two plastic chairs. A few steps away, painted on the polished floor, is the Brazilian flag. Solitary, it stands out. It is a sign of the hegemony of the Comando Vermelho (Red Command), a criminal organization known simply as the CV, whose name people are afraid to say aloud.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":164637,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/08/story5-ft1.jpg" alt="" class="wp-image-164637"/><figcaption class="wp-element-caption">A Brazilian flag is painted on the floor of the patio of the prison in Pucallpa, the capital of Peru’s Ucayali region. | Pamela Huerta</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>“There are ‘reds’ here, but I don’t know them,” one tells a visiting reporter. How does he know they are members of CV? “That’s what they say on the news. I don’t know anything. In my cell block there’s only one,” he adds nervously.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>He won’t say more and asks to change the subject, because those people are dangerous. “It’s better not to get involved with them,” he warns, while casting a covert glance at the tops of the cell blocks surrounding the patio. The prison currently houses 2,531 inmates, 2,418 men and 113 women, more than three times its official capacity of 800.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The CV is one of the most dangerous criminal organizations in Brazil. It has its roots in prison uprisings in the 1970s, which sought improved conditions in a now-defunct prison in Rio de Janeiro, but it soon morphed into a criminal organization that has made drug trafficking one of its main economic activities. The group began to take control of strategic cocaine trafficking routes, especially on the borders of Peru and Colombia. Tabatinga, the Amazonian city located at the triple border shared by those two countries and Brazil, was one of its key positions.</p>
