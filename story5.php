<p>That changed in 2020, when, in a quest for absolute control, it engaged in a bloody dispute with two other Brazilian crime groups, the Familia do Norte and the Primeiro Comando da Capital. All were weakened by the battle, allowing the rise of a new group of dissidents from the three gangs, who now operate as a consortium known as Os Crias in Portuguese and Las Crías in Spanish — terms meaning “The Kids.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>That same year, the world was paralyzed by the COVID-19 pandemic, and global markets, including illegal ones, faced critical situations. The CV, its dominion reduced, shifted toward Ucayali, a Peruvian region that borders Brazil, where illicit cultivation of coca, the main ingredient in cocaine, is expanding. </p>
