<!-- wp:paragraph -->
<p>Former regional government officials, who requested anonymity, confirm his account, saying they have been blocked and threatened by the CV when they have tried to start projects in the area.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Passenger boats travel to the Abujao River from Pucallpa’s port on the Ucayali River, a trip that takes between 10 and 15 hours depending on the time of year. To a visitor, the lowland scenery of forest and water looks the same, but an experienced boat pilot recognizes the subtle landmarks. In the dry season, when water levels are low, only canoes can navigate the Abujao and other tributaries of the Ucayali.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Sources in the National Police’s regional intelligence office say they began seeing Brazilians connected with the CV in the area in 2015, but it was in 2020 that they were able to confirm that the criminal organization had taken root in the region. They are cautious when identifying members of the CV, because some criminals, when arrested, will claim to belong to the group to inspire fear, they say.</p>
<!-- /wp:paragraph -->

