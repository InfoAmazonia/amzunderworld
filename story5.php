<h1 class="wp-block-heading" id="h-the-comando-vermelho-s-conquest">THE COMANDO VERMELHO'S CONQUEST</h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>María* is a young Shipibo-Konibo woman from an Indigenous community in the district of Callería, in Ucayali’s Coronel Portillo province. She was 19 when she met the man she still considers the love of her life. At the time, she had a partner and two children. “I was 12 when I got pregnant with the first,” she recalls. She was just a child, though she doesn’t think of it that way. She speaks in similar naive terms when she remembers the CV member who she says courted her and convinced her to get a tattoo of his name on her forearm, adorned with a heart and stretching nearly from the elbow to the wrist.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":164640,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/08/story5-ft2.jpg" alt="" class="wp-image-164640"/><figcaption class="wp-element-caption">A young Peruvian woman in prison in Pucallpa shows the tattoo of her drug trafficker boyfriend’s name, which she has tried to obliterate. | Pamela Huerta</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>She’s one of the 113 women in the prison on an early morning in April. Her red hair is tied high in a tousled topknot, and she’s wearing a print blouse that bares her shoulders. Her gold hoop earrings contrast with the pink lipstick she applied that morning.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Twenty-four now, she has spent the past three years in the Pucallpa prison for drug trafficking. They caught her trying to smuggle cocaine base paste into the prison for her brother-in-law, who was close to the man who became her boyfriend. “It was unfair,” she says. “I didn’t know. I trusted him, because he was family.”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>In her home community, coca is grown especially by those who speak Spanish best and can establish connections with <em>mestizos</em> — people who are not Indigenous. There aren’t many options for making a living, she says. “They choose our communities mainly because, since they’re so far away, the police never go there. Nobody goes there.”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>That area where no one goes, an area that is remote even for the government, and which law enforcement sources say is controlled by the CV, is the region around the Abujao River and the parallel Urtiquinía watershed, southeast of Pucallpa. Most of the communities are Indigenous, and people who have worked in the area say no one enters or leaves without the CV knowing.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“It’s been around six months since I was last there. It’s expensive to go, because of the cost of fuel. But I can enter and leave because I’m from there. I have my farm there, and I have family there,” says a farmer from Abujao who lives in Pucallpa now.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:cover {"customOverlayColor":"#d4c6ad","isDark":false,"align":"full","className":"tripa"} -->
<div class="wp-block-cover alignfull is-light tripa"><span aria-hidden="true" class="wp-block-cover__background has-background-dim-100 has-background-dim" style="background-color:#d4c6ad"></span><div class="wp-block-cover__inner-container"><!-- wp:quote {"className":"main-content"} -->
<blockquote class="wp-block-quote main-content"><!-- wp:paragraph -->
<p>If you go, they won’t let you enter, and they might not let you leave. The Brazilians control everything there.</p>
<!-- /wp:paragraph --></blockquote>
<!-- /wp:quote -->

