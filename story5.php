<!-- wp:paragraph -->
<p>María met the man whose tattoo she bears through his brothers, who began harvesting coca for a local drug trafficker when they were 12 years old. The brothers don’t know how to read or write, so they earn money picking coca leaves until their hands bleed. At first, they introduced him as José. Later she discovered that was not his real name. She says he treated her like a princess, although she describes him as an attractive man with no compunctions.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“I saw him every month, every time he came to pick up his shipment. He transported cocaine in bricks, in his wooden boat, so the police wouldn’t suspect anything, you know? So he let me know and I’d go to where he was staying. I’d stay three days and then he’d leave with his shipment,” she says, a look of nostalgia.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>According to law enforcement officials and other sources, who requested anonymity, the Abujao region is a liberated area controlled entirely by the CV, where cocaine base paste is produced and then transported by river to Brazil. One area controlled by the group centers around the rural community of Nuevo Utiquinía, on the Utiquinía River. Sources say it is an important point for stockpiling drugs for shipment, but it is difficult for authorities to gain access.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“The Brazilians hire residents of the community for their operations. Sometimes you see them walking through the community with their sacks or moving money. Everyone knows what they’re involved in, but you need to keep quiet or they’ll send someone to make you disappear. That’s what the community members themselves say over lunch or dinner,” says one former public servant who worked in the area around four years ago.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":164643,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/08/story5-ft3.jpg" alt="" class="wp-image-164643"/><figcaption class="wp-element-caption">The area planted in coca in Peru’s Ucayali region quadrupled between 2020 and 2022, according to official figures.&nbsp;| Alex Rufino</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>Col. Luis Wong Briceño, head of the Ucayali office of the National Police’s anti-drug unit, DIRANDRO, says there have been various police operations in the Shipibo-Konibo community of Flor de Ucayali on the Utiquinía River. Laboratories are destroyed, coca crops are eradicated, but they keep doing it,” he says.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>According to DEVIDA’s figures, Flor de Ucayali had 219 hectares (nearly one square mile) of coca crops in 2022, up 34% from 2020.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>DEVIDA has no statistics for other Indigenous communities in Ucayali, or for other rural communities known as campesino communities. According to Carlos Figueroa Henostroza, executive president of DEVIDA, the agency only monitors communities that request its assistance, and in Ucayali it works only with Flor de Ucayali. Acceptance of the agency’s help in developing other sources of income, however, does not necessarily mean that drug trafficking does not continue.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The lack of data raises questions about the accuracy of Peru’s estimates of coca production, as well as the amount of cocaine that can be produced from it. Peru’s official information system on drugs, known as SISCOD, only shows potential annual cocaine production for Ucayali through 2020, when, according to its figures, 35 tons were produced. There are no estimates for production since the presence of the CV in the region was confirmed.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading" id="h-the-lack-of-data-raises-questions-about-the-accuracy-of-peru-s-estimates-of-coca-production-as-well-as-the-amount-of-cocaine-that-can-be-produced-from-it">The lack of data raises questions about the accuracy of Peru’s estimates of coca production, as well as the amount of cocaine that can be produced from it</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Figueroa says the only way to try to eliminate the coca economy in the Abujao region would be to use the “Peru Model,” a strategy implemented in 2015, and recently revived, which involves eradication of coca crops, sanctions for illegal acts, and alternative development schemes to provide families with an income from legal sources, such as cocoa, coffee or fish farming.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“That is the only way to rescue vulnerable populations from illicit drug trafficking and include them in development,” he says.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Despite those efforts, however, Peru’s coca and cocaine production keep increasing. The remote communities most attractive to drug traffickers, where job and educational opportunities are scant or non-existent, are also far from markets for alternative crops. And no business is as profitable as drug trafficking or illegal gold mining, which has also moved into the Abujao watershed.</p>
