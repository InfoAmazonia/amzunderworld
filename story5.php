<!-- wp:paragraph -->
<p>Sources in the National Police’s regional intelligence unit confirm that the Brazilian CV members tend to be very reserved and avoid exposing themselves. They use Peruvian operators whom they monitor closely. Besides drug trafficking, the criminal organization is involved in murder for hire and arms trafficking, according to law enforcement&nbsp; sources in Ucayali.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“They basically use women to transport drugs. But by doing that you belong to the CV,” María says of the role of Peruvian women in the criminal organization. When her friend is released from prison, “The <em>vermelhos</em> are going to know. They’re going to find her and make her do the same thing,” she adds. “Once you’re in, it’s not easy to get out.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The CV’s control over territory in Ucayali would not be possible without Peruvians who are assimilated into the organization as producers and logistical operators in the Brazilian-led drug trafficking network. The leaders don’t leave loose ends or allow leaks. They control the entire chain, from supplies for production of cocaine base to the purchase and transportation of the merchandise.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Most of all, however, they control their members. If a local drug trafficker tries to sell his product to an organization other than the CV, they will find out and make him pay for his treason, law enforcement officials say. It’s not a matter of loyalty, but a form of control.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“They control the river routes, the chemical supplies, they have boats for logistics,” an agent in the regional police intelligence office says. “Basically, they come to Pucallpa to get supplies, they take them to the Abujao and sell them there to the <em>patrones</em> [drug traffickers] who manage the crops. But then the <em>patrones</em> have to sell them the drugs they produce, because they can’t move the drugs without [the CV] finding out. They have lookouts along the rivers and everything is reported.”</p>
<!-- /wp:paragraph -->

