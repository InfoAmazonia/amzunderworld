<blockquote class="wp-block-quote main-content"><!-- wp:paragraph -->
<p>They use women to transport drugs. But by doing that you belong to the CV. Once you’re in, it’s not easy to get out.</p>
<!-- /wp:paragraph --><cite>— Woman in prison for drug trafficking</cite></blockquote>
