<!-- wp:heading {"level":1} -->
<h1 class="wp-block-heading" id="h-drugs-shipped-by-land-river-and-air">DRUGS SHIPPED BY LAND, RIVER AND AIR</h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>In a wooden house with a palm-thatch room on the outskirts of Puerto Breu, in the eastern Yurúa district of the Ucayali region, two young men sit on the floor while one relaxes in a hammock. The three are alert to unwanted visitors. They’ve arrived from the town of Bolognesi, on the Ucayali River, traveling nearly 200 kilometers (124 miles) over the past five days, most of it on foot. Each has carried a 15-kilogram (33-pound) pack of drugs.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>They’ve been doing this for the past year, they say, going part of the way on motorcycles along a rough road that is muddy and rutted, but wide enough for logging equipment. They cross swift rivers in boats. But the longest stretch of the route, through dense forest, is a trail that takes them through mud, over streams and ravines, and past huge trees and coca fields. Mosquitoes and bees are their companions along the way, feeding on their sweat and blood.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Drugs manufactured in Ucayali are smuggled out by boat, in small planes and by drug runners like these three, known in Peru as “backpackers.” According to sources in DIRANDRO, the anti-drug police unit, the CV mainly ships drugs down the Ucayali River and the Amazon to towns like Tabatinga and Leticia, on the triple border with Colombia and Brazil. The same route is used by traffickers in the neighboring Loreto region. Other traffickers ship by small plane, mainly to Bolivia, from more than 70 landing strips carved out of Ucayali’s forests, the sources say. And some send smaller amounts overland with backpackers.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The house where the three men are staying near Breu belongs to a woman who lives in Pucallpa and has relatives in an Indigenous community in this area. It sits beside a red and yellow clay road that leads to the smallest and most remote of the town’s ports. From outside, the building looks abandoned, surrounded by weeds, without electric lines, not even a latrine for the visitors. The only signs of life are a few pieces of plastic hanging outside and the smoke from a small cook fire.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Two of the men are from Satipo, a town in Peru’s main coca-growing region, the Apurímac, Ene and Mantaro river valley, or the VRAEM, for its Spanish initials. The third is from San Francisco, in the upper Ene Valley, the heart of that region. Two young Ashéninka men from a community in the Yurúa district, who served as guides during the last stretch of the route, leave the house when a visitor begins asking questions.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":164646,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/08/story5-ft4.jpg" alt="" class="wp-image-164646"/><figcaption class="wp-element-caption">A woman walks past the port of Pucallpa, the capital of Peru’s Ucayali region. Drugs from the region are smuggled by river and by air, as well as by young men who travel on foot through the forest to the Brazilian border. | Pamela Huerta</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>It will be a few days before their contact, a Peruvian man who delivers the drugs to Brazilian traffickers, pays them and tells them they can return home. Payment depends on the shipment, and a group of five backpackers, can earn around US$2,800 for transporting 75 kilos (165 pounds) of cocaine. The pay rate depends on the distance and the weight of the load.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The three are among dozens of young men who transport drugs from production areas in Ucayali to Breu, where slim, high-powered motorboats take it to towns in Brazil. Based on accounts from young men who have been detained, police in Brazil have identified various points where Peruvian backpackers cross the border and traffickers stash drugs.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>According to police sources, the man who received the shipment from the three young backpackers harasses local Indigenous leaders who speak out about the expansion of drug trafficking in the area. During a border gathering of Indigenous residents of Peru and Brazil in April 2022, they say, he sat outside filming participants. Police confiscated his phone and erased the photos and videos, but filed no charges.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Police and local residents say traffickers offer “samples” of drugs to young people in small towns and Indigenous communities. They also recruit young people and some adults to work as guides for drug runners. One of the young Ashéninka men says 10 hours of walking was worth 100 soles (about $27). “What else are you going to do here?” he asks. Indeed, the only work is in subsistence agriculture or occasional temporary jobs in municipal offices, and there’s little chance of education past high school, or even grade school in some areas.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The backpackers talk about using their earnings to buy land around Breu.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“You can start a business. That way you don’t have to walk and can make money more easily, without so much pain in your legs,” one says with a laugh. Another adds, “A little bar or a shop. You can make a business out of anything. You just need the capital.”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>But getting out of the drug trafficking business can be far more difficult than getting in.&nbsp;</p>
