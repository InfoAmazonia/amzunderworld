<h1 class="wp-block-heading" id="h-the-peruvian-vermelhos">THE PERUVIAN VERMELHOS</h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>When María “fell into disgrace,” as she refers to her imprisonment in Pucallpa, she did not entirely understand what had happened. When she put together the pieces of the puzzle that led to her five-year sentence, she realized that the man she considered her boyfriend had something to do with it, but she wasn’t sure what. She denies that she still has feelings for him and shows the ink marks where she has tried to obliterate the tattoo with which he marked her. Like crossing out an error, but on her skin.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Does she know what the CV is? “Of course. He also belongs to that criminal network,” she says.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“They really scare me. They’re people who don’t like lies. They’re very disciplined in what they do. They don’t use cell phones or anything like that. They simply arrive at a place and people are waiting for them,” María says of the CV. She saw it up close, but says she never got involved, out of fear and out of love. At least, that’s what she believed until she was caught.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>In prison, she met another woman who was a member of the CV. They had similar stories. +</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“It’s dangerous to talk about them. They operate with codes,” María says, referring to the tattoos that show a person belongs to the organization, María says. It’s one of many mechanisms the group uses to control its members.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>When her friend joined the group, “They gave her a code,” or tattoo showing that she belongs to the organization, and which can always be recognized, María adds. “That code is still active, even though she’s in prison. Wherever she is, it will always be active and she must respond” if a CV member contacts her. </p>
<!-- /wp:paragraph -->

