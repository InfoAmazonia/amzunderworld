<p>In the discreet shadows of the trees and bushes on one of the numerous islands in the middle of the Caquetá River, Wilton feels at ease to talk about how he makes a living. After pulling the motorized canoe that brought him to the island onto dry land, he finds a fallen tree to sit on and lights a cigarette.When transporting drugs by river, pirates are a constant menace.</p>
