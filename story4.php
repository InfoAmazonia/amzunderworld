<!-- wp:paragraph -->
<p>In the Amazon, elders and shamans, who undergo decades of spiritual training, have traditionally employed rituals to heal the sick and ward off evil spirits. But now they are also sought out to protect those braving the dangers of the jungle while transporting marijuana and cocaine across international borders.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The shaman shows a small container of a dark-red paste called carayuru, which is made from a vine and possesses protective and preventive properties.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“You rub this on the belly button and lick it a little, and it’s like a defense,” he says. “They don’t look at you. You no longer face danger.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Mateo, the father of two young children, swears by the effectiveness of the ritual.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“It’s important because it helps give you the strength to keep going. In all the little jobs you do, there has to be protection. It has to be there,” he says.&nbsp;&nbsp;</p>
<!-- /wp:paragraph -->

