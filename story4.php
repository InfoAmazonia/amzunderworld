<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>For 30,000 Colombian pesos (about US$7) a kilo, a group of young men began carrying 50- to 60-kilogram (110- to 132-pound) loads of marihuana through the jungle to a dropoff point in Brazil, allowing the guerrillas and drug traffickers to avoid military border controls on the river.</p>
