<!-- wp:paragraph -->
<p>Moonlight seeps through an opening in the palm-thatch roof of the maloca, a circular communal house in the Indigenous village of La Pedrera, in a remote corner of southern Colombia. Seated in the center of the building, a Murui shaman chants and hums melodically, his eyes heavy-lidded in deep concentration.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The shaman passes around a white plastic container filled with green powder. Each of the young men in the circle scoops a pinch of the powder into his cheek and moistens it with saliva. The older man’s lips are coated with the powder, a mixture of pulverized coca leaves and ash called mambe, an integral part of rituals that helps the participants focus.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Sitting beside the shaman is Mateo,* a Murui man in his 30s dressed in shorts and flip-flops. Every time he prepares for a grueling three-week journey through the dense jungle, he comes to ask the shaman for spiritual protection. Along the way, he is used to facing not only venomous snakes, but also thieves and law enforcement patrols.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>On his journeys through the jungle, Mateo carries several dozen kilos of marijuana that belongs to a Colombian guerrilla group. His job is to deliver it to a Brazilian criminal organization.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":164512,"sizeSlug":"full","linkDestination":"none"} -->
<figure class="wp-block-image size-full" id="border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/08/story4-ft7.jpeg" alt="" class="wp-image-164512"/><figcaption class="wp-element-caption">Young men who transport shipments of drugs from Colombia to Brazil seek a shaman’s protection. | Photo credit: Alex Rufino</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>Although medical marijuana is gradually gaining legal status in various countries, the illicit drug economy is driven by demand for a potent form of marijuana known as skunk or “creepy.”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Trafficking in skunk — which skyrocketed in 2022, based on seizures by the Brazilian Federal Police — finances conflict and violence along the border between Colombia and Brazil. Recent crackdowns in Colombia highlight the severity of the problem, with authorities seizing record amounts of marijuana being transported to Brazil by river.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>In La Pedrera, a small village near the Brazilian border where there is a scant government presence and few jobs are available, Indigenous residents hire themselves out to armed groups to haul heavy loads of marijuana through the treacherous jungle.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>As the police officers wolf down canned meatballs with tomato sauce and dried cassava meal, the owner of the mining dredge arrives, a man known as Cabeludo, a nickname taken from the gray hair he ties back in a ponytail. The boat and dredge are both the miner's business and the home he shares with his wife, months-old baby, a cook and eight workers.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>He seems to take the uniformed intruders in stride, even when one pulls out a mobile phone and passes around a photo that is circulating in a WhatsApp group. It’s a corpse. Just hours earlier, he says, the owner of a mining dredge was murdered, allegedly by one of his workers, who fled into the jungle.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“He took half a kilo of gold with him,” one of the officers says.</p>
