<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Alvizu denies his group’s involvement in drug trafficking on the border and shifts the blame.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“There are criminal bands where Brazilian federal forces, military forces, along with the Colombian Navy, protect and help coordinate the drug trafficking chain in this border area,” he says. “It’s a very lucrative matter in this area, where Brazilian federal forces and Colombian military forces are involved. They act as transporters who facilitate drug trafficking.”</p>
