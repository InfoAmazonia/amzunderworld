<h1 class="wp-block-heading" id="h-the-only-way-out-is-to-carry-drugs">‘THE ONLY WAY OUT IS TO CARRY DRUGS’</h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Its location on the Caquetá River, which crosses the border into Brazil, makes La Pedrera a strategic transit point. The village has electricity from a diesel generator for only a few hours in the early afternoon and evening. But one of the riverside bars, surrounded by towers of crates of beer, stands out as an exception, blasting loud music throughout the village during most nights and days.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Besides the coffee-colored Caquetá, the Apaporis River is also nearby. Both rivers serve as crucial corridors for armed groups to transport troops, drugs and weapons. About 20 minutes upriver, local police and residents warn, there is a guerrilla checkpoint. So far, nobody has dared to interfere.</p>
