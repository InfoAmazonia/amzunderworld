<p>Despite allegations, locals make an effort to deny involvement in cocaine trafficking and often claim that their main source of sustenance is fishing.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>In confidential conversations, however, most admit that there is only one economy that actually keeps the village afloat.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“I’m telling you from experience that the only way out, here in Pedrera, is carrying drugs and marijuana,” says Wilton.*</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Wilton, who worked as a drug runner for about 10 years, grew up in a poor family, like most village residents. “As a family, we suffered from an economic crisis, where my father, may he rest in peace, was barely supporting us, until my God allowed him to rest, and that was the end of it,” he says.</p>
