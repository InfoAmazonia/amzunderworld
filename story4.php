<h1 class="wp-block-heading" id="h-brazilian-drug-trade-finances-farc">BRAZILIAN DRUG TRADE FINANCES FARC</h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Global attitudes toward marijuana are shifting, and countries including Italy and Australia have decriminalized possession of small amounts. Colombia legalized and regulated commercial cultivation of marijuana for medicinal and scientific use in 2016. In 2022, a Brazilian court approved the cultivation of cannabis for home medical use.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Nevertheless, most production for recreational use remains criminalized, so marijuana is sold on the black market. The trade is controlled by armed groups and criminal organizations that partly finance their violent campaigns with income from the illicit marijuana trade.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:image {"align":"center","id":164518,"width":450,"height":288,"sizeSlug":"full","linkDestination":"none"} -->
<figure class="wp-block-image aligncenter size-full is-resized"><img src="https://infoamazonia.org/wp-content/uploads/2023/08/bignumber_story4_01.jpg" alt="" class="wp-image-164518" width="450" height="288"/></figure>
<!-- /wp:image -->

<!-- wp:paragraph {"align":"center","className":"big no-margin"} -->
<p class="has-text-align-center big no-margin">In 2020,</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"textAlign":"center","level":1,"className":"touro no-margin bignumber"} -->
<h1 class="wp-block-heading has-text-align-center touro no-margin bignumber" id="h-brazil-ranked-third-in-the-world-in-marijuana-seizures">BRAZIL RANKED THIRD IN THE WORLD IN MARIJUANA SEIZURES,</h1>
<!-- /wp:heading -->

<!-- wp:paragraph {"align":"center","className":"big no-margin"} -->
<p class="has-text-align-center big no-margin">after India and the United States and Colombia took fourth place. Since the pandemic, demand formarijuana has only risen.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"align":"center","className":"caption"} -->
<p class="has-text-align-center caption">UNODC, responses to the annual report questionnaire</p>
<!-- /wp:paragraph -->

<!-- wp:spacer {"height":"62px"} -->
<div style="height:62px" aria-hidden="true" class="wp-block-spacer"></div>
<!-- /wp:spacer -->

<!-- wp:paragraph -->
<p>“Brazil has very active cartels that are constantly trying to contact dissident groups and drug traffickers in Colombia to acquire everything related to production of cocaine and marijuana,” says Harry Ernesto Reyna Niño, Colombian Navy chief of staff for force support, who was interviewed in June 2022, when he was vice admiral of the southern naval force.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Colombian guerrilla groups know where military controls are located and recruit residents of Indigenous communities for what authorities call hormigueo — “ant trafficking” — a reference to the way ants carry things through the forest. Local men transport loads destined for Brazil over forest paths and along small creeks.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Essentially, Reyna says, with the marijuana trade, Brazilian criminal organizations finance the violence perpetrated by armed groups in Colombia.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The trade in potent marijuana with high THC levels is attractive because of its high demand and value. While some of the marijuana is exported to Europe and eastern Africa, the bulk of the profit is made in the Brazilian market.&nbsp;</p>
