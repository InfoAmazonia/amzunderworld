<p>Whether he accepts a job depends on the shaman’s advice.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“No, if he says it’s dangerous to work, then you don’t go. You stay. And if he says it’s good, you go.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The abuelo says he does not demand payment for his spiritual services.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“For us, we learn that healing is not meant to be charged for,” he says. “We consider that to be wrong.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Nevertheless, he acknowledges that for other shamans, it’s a business. For somewhere between 150,000 and 200,000 Colombian pesos (US$35 to US$45), a trafficker can safely pass his drugs through the jungle.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Another shaman, originally from a community near the Apaporis River but now residing in Leticia, a Colombian town on the Amazon River, admits to consulting for drug runners.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“They hire me, each carrier,” he says, sipping on a mid-morning beer while watching the Amazon River from one of the bars near Leticia’s fresh produce market.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>One of his sons transports drugs through the jungle. The shaman acknowledges the presence of armed groups, but he is more concerned about soldiers who are on the lookout for drug traffickers. “I turn all their faces so they don’t look at the workers,” he says.</p>
