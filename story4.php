<h1 class="wp-block-heading" id="h-caught-between-drugs-and-farc">CAUGHT BETWEEN DRUGS AND FARC</h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Factions of the Revolutionary Armed Forces of Colombia (FARC) groups that did not sign the 2016 peace accords with the government operate around La Pedrera.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Dressed in military fatigues, a camouflage safari hat and rubber boots, a guerrilla leader who goes by the name of Danilo Alvizú is surrounded by heavily armed guerrilla troops during an appearance at a gathering in April in Llanos del Yarí, in Colombia’s Amazonian Caquetá department. Alvizú commands the Carolina Ramírez Front of the dissident group that calls itself the Estado Mayor Central-Revolutionary Armed Forces of Colombia (EMC-FARC).&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":164500,"width":810,"height":540,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full is-resized border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/08/story4-ft3.jpeg" alt="" class="wp-image-164500" width="810" height="540"/><figcaption class="wp-element-caption">The guerrilla known as Danilo Alvizú heads the Carolina Ramírez Front, a dissident FARC group accused of massacres in Colombia’s Putumayo region and drug trafficking along the Caquetá River. | Photo credit: Andrés Cardona</figcaption></figure>
<!-- /wp:image -->

<!-- wp:spacer {"height":"32px"} -->
<div style="height:32px" aria-hidden="true" class="wp-block-spacer"></div>
<!-- /wp:spacer -->

<!-- wp:paragraph -->
<p>Alvizú himself carries only a handgun. Prominently displayed on his right shoulder is the unmistakable FARC flag, which combines the yellow, blue and red of the Colombian flag with a map of the country adorned with crossed machine guns and a book.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>EMC-FARC claims to be the authentic FARC, in contrast to the groups that demobilized after signing a 2016 peace deal with the Colombian government. Alvizú actually signed the accord, but later joined FARC dissidents.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The EMC-FARC, commanded by Néstor Gregorio Vera Fernández, alias Iván Mordisco, has grown in size and expanded its territorial control since 2020, as law enforcement operations became more sporadic during the COVID-19 pandemic.&nbsp;</p>
