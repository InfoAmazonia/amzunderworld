<!-- wp:heading {"level":1} -->
<h1 class="wp-block-heading" id="h-prime-location-for-exports"><strong><strong><strong>PRIME LOCATION FOR EXPORTS</strong></strong></strong></h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The prime location of Vila do Conde port, just 120 kilometers (75 miles) from the Atlantic Ocean, makes it ideal for exporters sending commodities to Europe, China and the United States. In 2021, nearly 17 million tons of cargo such as soybeans, alumina, bauxite, fertilizer and corn passed through the port.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>It is one of the so-called “Northern Arc” ports that gained prominence in Brazil over the last decade, which shorten maritime distances to key markets and therefore cut logistics costs, challenging the traditional dominance of São Paulo’s Santos port. But criminal organizations have also capitalized.</p>
<!-- /wp:paragraph -->

