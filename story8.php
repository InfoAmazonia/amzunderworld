<h1 class="wp-block-heading" id="h-gold-amp-diamonds-a-magnet-for-miners"><strong><strong><strong>GOLD &amp; DIAMONDS A MAGNET FOR MINERS</strong></strong></strong></h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p><strong>By Sam Cowie in Barcarena</strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Maria talks fondly about the peaceful rural community where she grew up. As a child, she recalls playing games in the forest and in the river with the other kids, picking fresh açaí berries and foraging for nuts under towering trees. There were no “industrialized toys,” she says. It was “all nature.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>But in the last few years, with the arrival of a group of drug-trafficking outsiders, that reality has changed to nighttime gunshots, police raids and talk of murder. Late last year, after a young man came to her home and threatened her, Maria* decided she had to leave.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“He said that if I snitched and had them taken out of there, I would bleed—my whole family would,” she says.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The man who threatened Maria suspected, correctly, that she had told local authorities about the criminal group’s activities. Fearing for her life, she fled the community called Sítio Cupuaçu, home to around 350 families, in Barcarena, a port city and today a major industrial hub close to where the Amazon River meets the Atlantic Ocean, in the state of Pará.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“We were terrified,” she says. “People who were born and raised here, we never heard of this drug business.” Her community is one of several <em>quilombos —</em> settlements formed by African people who escaped slavery in Brazil, and people of African descent — in the region.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":165013,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/08/story8-ft2.jpeg" alt="" class="wp-image-165013"/><figcaption class="wp-element-caption">A man stands beside the river in the port city of Barcarena, Brazil, which has become a new hub for international shipment of illegal drugs. | Wagner Almeida</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>Maria says the people invading her community claim to have “permission” from the leader of a local branch of Brazil’s notorious Comando Vermelho (CV), or Red Command, a drug gang with its main base more than 3,000 kilometers (1,864 miles) away in Rio de Janeiro. In the last five years, it has established itself as the dominant drug trafficking group in Pará.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The gang used the forested community as a distribution and storage point for drugs like cocaine, crack and marijuana, burying larger quantities in the ground in case of police raids.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Besides selling drugs, the CV also engaged in small-scale land grabbing, clearing land to divide into plots to sell and illegal construction, wrecking the community’s rich natural habitats.&nbsp;&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“Trees that were hundreds of years old, they cut them down,” Maria said, showing pictures of fallen forest and foliage on her smartphone. “The animals left. It’s really sad.”</p>
<!-- /wp:paragraph -->

