<!-- /wp:image -->

<!-- wp:paragraph {"align":"center","className":"big no-margin"} -->
<p class="has-text-align-center big no-margin">Data obtained through a freedom of information request by InfoAmazonia to Brazil’s federal revenue agency show that</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"textAlign":"center","level":1,"className":"tvermelho no-margin bignumber"} -->
<h1 class="wp-block-heading has-text-align-center tvermelho no-margin bignumber" id="h-the-volume-of-cocaine-seized-at-vila-do-conde-increased-from-1-462-kilos-in-2019-to-1-870-kilos-in-2021">THE VOLUME OF COCAINE SEIZED AT VILA DO CONDE INCREASED FROM 1,462 KILOS IN 2019 TO 1,870 KILOS IN 2021,</h1>
<!-- /wp:heading -->

<!-- wp:paragraph {"align":"center","className":"big no-margin"} -->
<p class="has-text-align-center big no-margin">though experts have warned that seizures alone would not necessarily reflect an accurate picture.</p>
<!-- /wp:paragraph -->

<!-- wp:spacer {"height":"40px"} -->
<div style="height:40px" aria-hidden="true" class="wp-block-spacer"></div>
<!-- /wp:spacer --></div>
<!-- /wp:group -->

<!-- wp:image {"id":165016,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/08/story8-ft3.jpeg" alt="" class="wp-image-165016"/><figcaption class="wp-element-caption">The Vila do Conde port’s infrastructure and the volume of commodities passing through help facilitate the shipment of illegal drugs. | Wagner Almeida</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>In 2020, military police officers in Barcarena seized more than two tons of cocaine at a rural property a short drive from the port — and from Maria’s home — after exchanging gunfire with armed men who then fled into the nearby forest. At the time it was the largest cocaine seizure in Pará’s history.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Then, in November 2022, federal authorities seized nearly three tons of cocaine hidden inside sacks of soybean meal in a container at the port and destined for Portugal. It was one of the largest single cocaine busts ever recorded at a Brazilian port, according to local reports.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Authorities have also seized cocaine hidden in shipments of timber, açai and manganese — a mineral that has been increasingly coveted and extracted illegally in southern Pará in recent years by organized mafia groups — ready to be exported or arriving from Vila do Conde port to European ports in Portugal and Rotterdam.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>While São Paulo’s Santos port is still the main exit point for cocaine headed to Europe, inspections have stepped up in recent years, forcing criminal groups to seek other ports, according to the U.N. Office on Drugs and Crime (UNODC).</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The seizures at Vila do Conde reflect part of a reconfiguration of drug routes and exit points for cocaine passing through Brazil on its way to Europe, as well as the growing importance of the Amazon as an international drug transit point.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>In the last decade, experts say, the volume of cocaine and other drugs passing through the Brazilian Amazon, whether destined for local markets or for export, has risen dramatically, with record production levels in Brazil’s neighbors Colombia, Peru and Bolivia.</p>
<!-- /wp:paragraph -->

