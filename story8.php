<!-- wp:paragraph -->
<p>Brasil described a complex picture of warehouses used in the Barcarena area to store cocaine, often with the tacit permission of corrupt local police, and front companies set up solely to give an air of legitimacy to the drug trade.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“It is an institution to defend public order and morality,” he says of the police, “but unfortunately there are some who enter to steal, to extort to benefit themselves.”</p>
