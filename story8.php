<!-- /wp:image -->

<!-- wp:heading {"level":1} -->
<h1 class="wp-block-heading" id="h-shipping-by-river-road-and-air"><strong><strong><strong>SHIPPING BY RIVER, ROAD AND AIR</strong></strong></strong></h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Pará neither produces cocaine nor borders producer nations. According to state Public Security Secretary Ulame Machado, most cocaine and other drugs entering Pará, especially shipments bound for the port in Barcarena, come from the triple border with Peru and Colombia in neighboring Amazonas state and arrive by river.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“Skunk,” a high-grade form of marijuana produced in Colombia, sells in Brazil for five times the price of traditional, weaker weed imports from Paraguay and enables criminal groups already using the Amazon River route to increase their profits, he says.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":165022,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/08/story8-ft5.jpg" alt="" class="wp-image-165022"/><figcaption class="wp-element-caption">Police search suspects during a sweep of an area of Barcarena. In Pará, drug trafficking is increasingly intertwined with illegal gold mining, land grabbing and environmental crime. | Wagner Almeida</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>Reports of such shipments&nbsp; headed directly for Brazil’s largest drug markets, São Paulo and Rio de Janeiro, have proliferated recently.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Machado mentioned a January seizure of 1.6 tons of cocaine and skunk hidden beneath 12 tons of prized pirarucu fish, from a vessel near Óbidos, in the Lower Amazon, a key region for the entry of drugs into Pará. From Óbidos, drugs can be loaded into trucks and transported by road, especially the BR-163 and TransAmazonian highways. From there, “you can deliver to the whole of Brazil,” including the Vila do Conde port, Machado says,&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“It is much more profitable, much less risky for them to use Barcarena port,” he says. “And at the same time their logistics are much easier, because you don't have to cross the whole country to get to Santos.” Bolivian cocaine passing through Mato Grosso also enters southern Pará by road, as well as by plane, he says.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>An uptick in illegal mining in Pará in recent years, driven by record high gold prices, has increased the number of small plane flights, says Gustavo Geiser, an expert witness with Brazil’s Federal Police. “And since these guys are already doing something illegal,” he adds, delivering cocaine “is not a huge step up for them.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Recently, Pará-born Felipe Pacheco, an illegal mining pilot from Novo Progresso, a notorious hotbed of environmental crimes like wildcat mining and illegal logging, was <a href="https://g1.globo.com/pa/para/noticia/2023/02/28/piloto-preso-no-mt-com-465-kg-de-cocaina-e-ligado-ao-garimpo-em-novo-progresso-no-pa.ghtml">caught transporting</a> nearly half a ton of cocaine in his plane in Mato Grosso state. He was <a href="https://www.rdnews.com.br/policia/conteudos/172601">found dead in jail</a> days later, and his family believes he was murdered.</p>
<!-- /wp:paragraph -->

