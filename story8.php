<!-- /wp:image -->

<!-- wp:heading {"level":1} -->
<h1 class="wp-block-heading" id="h-gold-amp-diamonds-a-magnet-for-miners-0"><strong><strong><strong>GOLD &amp; DIAMONDS A MAGNET FOR MINERS</strong></strong></strong></h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Just under half of the almost 2,000 tons of cocaine produced annually passes through Brazil, roughly 40 percent of it through the Amazon region, according to Renato Sérgio de Lima, head of the non-profit Brazilian Public Security Forum.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“The turning point was in 2016,” he says, with the anti-aircraft machine gun <a href="https://www1.folha.uol.com.br/internacional/en/brazil/2016/06/1782734-four-hour-military-battle-kills-drug-boss-on-the-border-of-paraguay.shtml">murder of Jorge Rafaat</a>, who controlled the supply of drugs on Brazil’s border with Paraguay, by the PCC, which then took over the route. “The Comando Vermelho needed to adapt and was forced to find new routes, reactivating old contacts it had with Colombian drug lords.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>While Brazil’s Amazon has always been violent, de Lima says, violence has exploded since 2017. The region offers various advantages to drug traffickers.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“It has less inspection and control, very cheap and exploited labor, and the possibility of exploiting various sectors of the economy in a much easier way than big cities,” de Lima says. He adds, “It’s much simpler there because there's less [state] supervision, so it’s very easy to launder money.”</p>
<!-- /wp:paragraph -->

<!-- wp:spacer {"height":"32px"} -->
<div style="height:32px" aria-hidden="true" class="wp-block-spacer"></div>
<!-- /wp:spacer -->

<!-- wp:image {"id":164986,"width":190,"sizeSlug":"full","linkDestination":"none"} -->
<figure class="wp-block-image size-full is-resized"><img src="https://infoamazonia.org/wp-content/uploads/2023/08/story8_icones_criminalsdodge.jpg" alt="" class="wp-image-164986" width="190"/></figure>
<!-- /wp:image -->

<!-- wp:heading {"level":1} -->
<h1 class="wp-block-heading" id="h-criminals-dodge-the-police"><strong><strong><strong>CRIMINALS DODGE THE POLICE</strong></strong></strong></h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>In January, Amazon Underworld joined local military police officers in Barcarena on an operation to clamp down on drug trafficking and other crimes. Officers said that in recent years, the city had seen an uptick in gang members released from prison heading there to set up local chapters of crime groups like the Comando Vermelho.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Barcarena is home to several traditional rural <em>quilombos</em> like the one Maria fled, where officers say that gangsters from outside the city have sought to establish themselves.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The day before the operation, officers said they had raided a forest encampment set up by drug traffickers in the Itupanema Beach region of Barcarena. Across the city’s poorer neighborhoods, Comando Vermelho graffiti is omnipresent.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>During the patrol, armed officers stormed a simple wooden house and arrested a man accused of rape who had fled from another city in Pará. Ultimately, however, the operation yielded no drug crime arrests.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":165010,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/08/story8-ft1.jpeg" alt="" class="wp-image-165010"/><figcaption class="wp-element-caption">Police in Barcarena search a suspect during a nighttime patrol. | Wagner Almeida&nbsp;</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>One person present on the patrol, who requested anonymity, said that with the patrol ongoing, gang members would send the message round that police were out and therefore lay low. In April, police in Barcarena arrested a 34-year-old man wanted for homicide. During the operation, they also seized around R$68,000 (equivalent to about $13,600) in cash, as well as cocaine, crack, a .12-caliber shotgun, a .40-caliber machine gun, two .9- and .40-caliber pistols, and two vehicles.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Police allege that the man, whose identity was not revealed, planned attacks against security authorities during a wave of violence orchestrated by Comando Vermelho leaders last year, in which at least seven police officers were killed. Shortly after, in July, 10 military police officers in Belém, the capital of Pará, were arrested on charges of kidnapping and extorting drug traffickers.</p>
<!-- /wp:paragraph -->

