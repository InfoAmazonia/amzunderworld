<h1 class="wp-block-heading" id="h-we-have-nothing-left"><strong><strong><strong>‘WE HAVE NOTHING LEFT’</strong></strong></strong></h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Investigations cited in local reports indicate that Freitas, the military police officer arrested in Portugal, <a href="https://noticias.uol.com.br/colunas/josmar-jozino/2022/11/05/pf-apreende-27-t-de-cocaina-no-pa-a-maior-realizada-em-portos-do-brasil.htm">worked for</a> Portugal’s most powerful drug lord Ruben “Xuxas” Oliveira and Carvalho, the former military police major from Mato Grosso do Sul, then considered Brazil’s biggest independent drug trafficker.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Interpol agents <a href="https://noticias.uol.com.br/colunas/josmar-jozino/2022/06/22/de-bermuda-e-tomando-cafe-no-hotel-assim-foi-preso-o-escobar-brasileiro.htm">arrested Carvalho</a>, also nicknamed “Brazil’s Escobar” in a luxury hotel in Budapest in June 2022. Since 2017, according to local reports citing federal investigations, Carvalho had sent <a href="https://noticias.uol.com.br/internacional/ultimas-noticias/2022/06/22/major-carvalho-escobar-brasileiro-quem-e.htm">at least 45 tons of cocaine</a> to Europe.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Karine Campos, nicknamed the “Queen of Coke,” a colleague of Major Carvalho’s and today considered Brazil’s largest drug boss, is also accused of <a href="https://www.mpf.mp.br/rn/sala-de-imprensa/noticias-rn/mpf-denuncia-54-pessoas-por-trafico-e-posse-de-pelo-menos-14-toneladas-de-cocaina">leading a group</a> of international drug traffickers that used the Vila do Conde Port to send cocaine to Europe. Currently she is on the run, <a href="https://noticias.uol.com.br/colunas/josmar-jozino/2022/07/14/familia-busca-po-comanda-trafico-em-portos-do-brasil-e-esta-na-mira-da-pf.htm">sentenced to 17 years</a> in prison for drug trafficking, along with her husband Marcelo Ferreira.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":165025,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/08/story8-ft6.jpeg" alt="" class="wp-image-165025"/><figcaption class="wp-element-caption">A cyclist travels across a clandestine waste disposal site in Barcarena. | Wagner Almeida</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>Meanwhile, back in Barcarena, a fisherman beaches a rickety wooden boat at Itupanema, a short drive from the Vila do Conde port, just as an intense Amazonian rainstorm begins. In the distance, hulking cargo ships stacked with steel containers filled with tons of commodities headed for international markets sit anchored as the storm beats down.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Maria understands little about the dynamics of international cocaine trafficking but knows the damage that has been inflicted on the forest community nearby where she was born and raised.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“We have nothing left,” she says. “If one day we manage to get this land back, we’ll have to start all over again.”</p>
