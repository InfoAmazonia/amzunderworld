<!-- wp:paragraph -->
<p>One of Brazil’s most experienced drug trafficking pilots, Silvio Berri Júnior, who worked for infamous Comando Vermelho leader Fernandinho Beira-Mar, held a gold prospecting claim in Pará and <a href="https://oglobo.globo.com/brasil/piloto-de-beira-mar-alvo-de-narcos-gold-tem-licenca-do-governo-para-garimpar-equivalente-800-campos-de-futebol-na-amazonia-1-25294109">was arrested in 2020</a> on suspicion of working for mega trafficker Major Carvalho.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Machado says Pará also serves as an important base for traffickers transporting drugs by air to other regions of Brazil, as the vast state is often used for refueling stops. Indigenous territories in Pará, like those of the Kayapo and Munduruku, which suffer some of the worst destruction from illegal mining in the Brazilian Amazon, also serve as logistics points for drug flights, Machado said.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“Such places are convenient [for drug traffickers] because they are out of the way of the public eye,” he adds. “There are no police cars patrolling there. They are remote and isolated.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>In illegal mining towns and encampments across Pará, cocaine and crack fetch far more than the average street price — swaps of one gram of gold for one gram of cocaine are common.</p>
<!-- /wp:paragraph -->

