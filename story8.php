<!-- wp:heading {"level":1} -->
<h1 class="wp-block-heading" id="h-drug-gangs-spread-beyond-cities"><strong><strong><strong>DRUG GANGS SPREAD BEYOND CITIES</strong></strong></strong></h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Today, enrolled in a local program that provides protection and assistance for threatened activists in Pará, Maria laments the destruction of her community’s natural beauty and its social fabric.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“It was so good,” Maria says of the community. “We always had issues with <em>grileiros</em> [large scale land grabbers] trying to claim our land, but they always backed off eventually when we showed them our land titles.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>She says the drug gang has also co-opted some community members and young people with small gifts and promises of easy money.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Her cousin, who was head of the local resident’s association, also fled the community after the gang threatened him with a gun at his house.</p>
<!-- /wp:paragraph -->

<!-- wp:cover {"customOverlayColor":"#d4c6ad","isDark":false,"align":"full","className":"tripa"} -->
<div class="wp-block-cover alignfull is-light tripa"><span aria-hidden="true" class="wp-block-cover__background has-background-dim-100 has-background-dim" style="background-color:#d4c6ad"></span><div class="wp-block-cover__inner-container"><!-- wp:quote {"className":"main-content"} -->
<blockquote class="wp-block-quote main-content"><!-- wp:paragraph -->
<p>We’re good people who want to raise our children well. Now the best way for us to raise our children is to send them outside of the community</p>
<!-- /wp:paragraph --><cite>— Maria, a resident of the <em>quilombo</em> Sítio Cupuaçu</cite></blockquote>
<!-- /wp:quote -->

<!-- wp:spacer {"height":"16px"} -->
<div style="height:16px" aria-hidden="true" class="wp-block-spacer"></div>
<!-- /wp:spacer --></div></div>
<!-- /wp:cover -->

<!-- wp:paragraph -->
<p>In 2019, a policeman was killed in the community, and one of those later arrested in connection with the murder had already been charged with international drug trafficking. Last year, two Comando Vermelho gang members were also killed there, in a raid that happened during the wave of attacks on police officers.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Maria said that the young man who threatened her at her home is the second in command in the area and handles sales transactions involving the clandestine constructions. She said his mother is from the community.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The gangsters removed the community plaque, so as not to put off buyers, and one of the threats Maria received came after she told a prospective buyer that the area was a <em>quilombo</em>, and that his purchase might later be revoked, as those lands could qualify for government protection in the future.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The person who came to her home “had a .38 revolver,” she says. Although they have not killed any community members, she adds, “They are always killing among themselves.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“When the police come in, they disappear,” she says.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The dynamics of “invasions,” as illegal land occupations run by organized crime groups are locally known, are murky and often differ.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The Belém Metropolitan Region has some of the worst affordable housing provisions in Brazil, a country in perpetual housing crisis, so there is no lack of willing occupiers. One resident who lives next to a Comando Vermelho invasion in Ananindeua, another city in the Belém Metropolitan Region, says local gang leaders decide what lands can be invaded and by whom.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The tactic serves to further dominate and expand urban frontiers in a region where the Comando Vermelho’s only armed rivals are police and rogue police militias. Anyone who breaks the rules, by renting or selling without permission, for example, faces a “tribunal” which can result in their murder or expulsion from the invasion without being compensated.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>According to Aiala Colares Couto, a researcher with the Brazilian Public Security Forum and an expert on organized crime in Pará, the growing criminal movement in Barcarena follows a statewide pattern.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":165019,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/08/story8-ft4.jpg" alt="" class="wp-image-165019"/><figcaption class="wp-element-caption">Police rush to arrest a suspect in an area of Barcarena, in the Brazilian state of Pará. | Wagner Almeida</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>Gang leaders, known as <em>torres</em>, or “towers,” many recently released from prison or wanted by authorities, head to more remote locations outside of state capitals and city centers to avoid police detection.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“When they arrive, they need to maintain a relationship with drug trafficking, because they need to support themselves economically,” he says.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Couto says the CV in Barcarena would almost certainly seek to expand its influence in the port region, just as the PCC did years ago in São Paulo’s Santos port, where it now wields significant influence.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>He said the CV spread rapidly in Pará, recruiting members within the state’s brutal prison system after Pará prisoners were jailed with CV members in federal prisons elsewhere in Brazil.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>CV gangsters from Pará have also increasingly fled to Rio de Janeiro. Leonardo Costa Araújo, a gang leader known as Leo 41, was killed in March in a police operation that left 13 dead in Complexo do Salgueiro, in São Gonçalo, in the Rio Metropolitan Region.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The PCC, the CV’s main national rival, also operates in Pará, mainly in drug trafficking logistics, mostly in the south of the state. Recently, authorities raided properties connected to suspected lawyers of the group in the cities of Marabá and Castanhal. Smaller gangs, as well as several militia groups involving rogue police officers, also operate in the state.</p>
<!-- /wp:paragraph -->

<!-- wp:cover {"customOverlayColor":"#d4c6ad","isDark":false,"align":"full","className":"tripa"} -->
<div class="wp-block-cover alignfull is-light tripa"><span aria-hidden="true" class="wp-block-cover__background has-background-dim-100 has-background-dim" style="background-color:#d4c6ad"></span><div class="wp-block-cover__inner-container"><!-- wp:quote {"className":"main-content"} -->
<blockquote class="wp-block-quote main-content"><!-- wp:paragraph -->
<p>Barcarena’s proximity to Belém, its high GDP and infrastructure, including the port, make it attractive for drug gangs, while the region’s traditional, often impoverished, communities grow increasingly vulnerable.</p>
<!-- /wp:paragraph --></blockquote>
<!-- /wp:quote -->

<!-- wp:spacer {"height":"16px"} -->
<div style="height:16px" aria-hidden="true" class="wp-block-spacer"></div>
<!-- /wp:spacer --></div></div>
<!-- /wp:cover -->

<!-- wp:paragraph -->
<p>Besides rising crime, several major industrial disasters since 2010 have seriously damaged local habitats on which traditional communities depended.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>In 2015, a Lebanese cargo ship headed for Venezuela with 5,000 cows and 700 tons of oil sank at the Vila do Conde port, killing most of cattle and releasing part of the oil into the river. Studies found the accident seriously depleted fishing stocks, damaging local livelihoods.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Three years later, residents complained to prosecutors that they suspected toxic leakage from the world’s largest aluminum smelter, owned by the Norwegian-financed company Norsk Hydro, had further poisoned local rivers, a claim later <a href="https://www.mpf.mp.br/pa/sala-de-imprensa/paginas-especiais/paginas-caso-hydro/historico">supported by studies</a>. Shortly after, <em>quilombo</em> leader Paulo Nascimento was murdered. Lawyers and activists said his <a href="https://www.theguardian.com/world/2018/mar/16/brazil-pollution-amazon-aluminium-plant-norwegian">killing was connected </a>to the complaint, but the company denied involvement.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“What we see is the increasing suffocation of Barcarena’s traditional communities by both crime and heavy industry, the destruction of their traditional ways of life,” says Cícero Pedrosa, a journalist and anthropologist who has studied Barcarena’s <em>quilombos</em>. “How can these families have a healthy life there?”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:spacer {"height":"32px"} -->
<div style="height:32px" aria-hidden="true" class="wp-block-spacer"></div>
<!-- /wp:spacer -->

