<!-- wp:heading {"level":1} -->
<h1 class="wp-block-heading" id="h-drug-trade-impossible-without-corruption"><strong><strong><strong>DRUG TRADE ‘IMPOSSIBLE’ WITHOUT CORRUPTION</strong></strong></strong></h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Machado says efforts are being made to stop the flow of cocaine into Pará, including the construction of civil police river bases in the Obidos region and Abaetetuba, two key municipalities on the Amazon River drug route, as well as the recent construction of a base in Breves, in the Marajó Archipelago near the mouth of the Amazon.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The greater Belém area, where Vila do Conde port is located, consists of around 40 islands, many harboring “clandestine” ports where drugs can be unloaded. Barcarena itself is an island. Drug shipments pass by riverine communities and <em>quilombos</em>, Machado says, and authorities often find stashes buried in the ground in and around Barcarena.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Large quantities of drugs are spread out in smaller amounts in rural properties or warehouses that are cheaper and more protected because they are isolated. “But to enter the Vila do Conde port, it has to be professionalized,” he says.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Legitimate exporters may unknowingly have their containers hacked and filled by specialized criminal gangs working at ports, a technique known as “rip on/rip off,” when official seals are broken and then reapplied.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>But “professionalized” drug shipments, like the 2.75 tons of cocaine found hidden in soybean meal in 2022, often require the help of corrupt public officials, experts say.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>In July 2022, Pará state military police Lieutenant Aderaldo Pereira de Freitas Neto <a href="https://g1.globo.com/pa/para/noticia/2023/04/05/promotoria-de-justica-pede-afastamento-de-tenente-da-pm-do-para-suspeito-de-participacao-em-trafico-internacional-de-drogas.ghtml">was arrested</a> in Portugal, suspected of conspiring to have 320 kilograms of cocaine sent to Portugal from Vila do Conde port in containers of açai fruit. Marco Antonio Faria, a Barcarena based businessman, was also <a href="https://www.segurancaportuariaemfoco.com.br/2022/10/paraenses-presos-em-portugal-confirmado.html">arrested</a> when he received the cargo in Lisbon.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Armando Brasil, a Pará state military prosecutor, who officially requested Freitas’ removal from service, says international drug trafficking would be “impossible” without corrupt public agents. He notes that the police lieutenant was supposed to be on duty in Pará at the time he was arrested in Portugal</p>
<!-- /wp:paragraph -->

