<!-- /wp:image -->

<!-- wp:heading {"level":1} -->
<h1 class="wp-block-heading" id="h-laundering-money-through-environmental-crime"><strong><strong><strong>LAUNDERING MONEY THROUGH </strong>ENVIRONMENTAL<strong> CRIME</strong></strong></strong></h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>While Barcarena has emerged as a key drug trafficking hub, in recent years, the CV has expanded even further into Pará’s vast interior, where violence has spiked. Today, the state is home to seven of Brazil’s 30 most murderous municipalities.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Jacareacanga, a hub of illegal mining activity and where the Munduruku Indigenous Territory is located, recorded a stunning 199 homicides per 100,000 inhabitants in 2021, the second-highest rate in Brazil and nine times the national average of 22 per 100,000.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Other municipalities in the top 30, including Floresta do Araguaia, Cumaru do Norte, Anapu, Senador José Porfírio, Novo Progresso and Bannach, also suffer from high rates of environmental crime and land conflict.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Today, drug gangsters in Pará and across the Amazon increasingly look to reinvest or launder illicit profits through environmental crimes such as wildcat mining, land grabbing, illegal fishing and poaching, which typically carry lesser penalties than drug crimes.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>A 2021 police operation called&nbsp; “Narcos Gold” confirmed that criminals used gold mines in Pará as landing strips for planes transporting drugs and as a front for money laundering.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The main target of the operation, Heverton “Grota” Soares, held 18 prospecting permits for mining in Pará which he used as a drug trafficking front and is investigated for drug trafficking, criminal organization, money laundering and homicide and has links to the PCC, <a href="https://oglobo.globo.com/brasil/seguranca-publica/avioes-joias-cavalgadas-shows-de-famosos-como-narcogarimpo-movimenta-dinheiro-na-amazonia-25272441">according to media reports</a>.</p>
<!-- /wp:paragraph -->

<!-- wp:spacer {"height":"32px"} -->
<div style="height:32px" aria-hidden="true" class="wp-block-spacer"></div>
<!-- /wp:spacer -->

<!-- wp:image {"id":165007,"width":190,"sizeSlug":"full","linkDestination":"none"} -->
