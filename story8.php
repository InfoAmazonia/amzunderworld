<!-- /wp:paragraph -->

<!-- wp:paragraph {"className":"tbranco"} -->
<p class="tbranco">Across Pará, traditional communities — Indigenous villages, <em>quilombos</em> and other riverine settlements — have long been threatened by loggers, land grabbers and illegal miners. Now they face growing pressure from drug-trafficking gangs, as the state becomes an increasingly important entry and exit point, a mandatory “corridor,” for cocaine passing through Amazonia and heading to domestic markets and Europe.&nbsp;&nbsp;</p>
