<h1 class="wp-block-heading has-text-align-center tverdeescuroa no-margin bignumber" id="h-which-at-1-2-million-square-kilometers-is-more-than-5-times-the-total-land-size-of-the-united-kingdom">WHICH AT 1.2 MILLION SQUARE KILOMETERS* <br>IS MORE THAN 5 TIMES THE TOTAL LAND SIZE OF THE UNITED KINGDOM</h1>
<!-- /wp:heading -->

<!-- wp:paragraph {"align":"center","className":"big no-margin"} -->
<p class="has-text-align-center big no-margin">— by river, highway or clandestine flights that have proliferated with an uptick in illegal mining in Pará and across Brazil’s Amazon in recent years. *(more than 460,000 square miles)</p>
<!-- /wp:paragraph -->

<!-- wp:spacer {"height":"40px"} -->
<div style="height:40px" aria-hidden="true" class="wp-block-spacer"></div>
