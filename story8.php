<!-- wp:paragraph -->
<p>Both of the country’s main drug gangs, the CV and its more powerful rival, the São Paulo-based Primeiro Comando da Capital (PCC), or First Capital Command, have expanded their reach inside the state, experts say. Some of Brazil’s largest independent traffickers, such as Sérgio Roberto de Carvalho, known as “Major Carvalho,” a former military police officer <a href="https://www.interpol.int/en/News-and-Events/News/2022/Alleged-drug-kingpin-arrested-in-Hungary">accused of heading a major Brazilian drug-running network</a>, and Karine Campos, <a href="https://www1.folha.uol.com.br/internacional/en/brazil/2023/03/cocaine-queen-has-been-on-the-radar-of-brazilian-police-since-2011.shtml">known as the “Queen of Coke,”</a> have operated there too, local reports suggest.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Once inside Pará, gangsters divide cocaine cargoes to send to domestic markets in the state or across the country, or to Europe using Brazil’s expansive port structures, including Pará’s Vila do Conde, a short drive from Maria’s house.</p>
<!-- /wp:paragraph -->

<!-- wp:spacer {"height":"32px"} -->
