<h1 class="wp-block-heading" id="h-gold-feeds-extortion-corruption-violence">Gold feeds extortion, corruption &amp; violence</h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Before the 1990s, most of the illegal gold mining in this region, where Colombia’s Puré River becomes Brazil’s Rio Puruê and flows into the Japurá, was done by small-scale prospectors. But with the rise in gold prices in the early 2000s, organized crime groups cashed in on the lucrative industry, including Colombian guerrillas who saw illegal gold mining as a way to launder profits from the drug trade.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>After a 2016 peace deal between the Colombian government and the Revolutionary Armed Forces of Colombia (FARC), a dissident guerrilla group began building a new criminal enterprise, financing their ongoing armed violence in part by shaking down illegal miners for what they called “voluntary contributions.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Some time back, the Brazilian Army faced off with guerrillas on the Puruê River, recalls Getúlio,* a miner from São Paulo who operates an industrial-scale dredge — called a draga in Portuguese and Spanish — about six times the size of Cabeludo’s.</p>
