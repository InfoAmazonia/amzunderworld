<!-- /wp:image -->

<!-- wp:paragraph -->
<p>As the police officers wolf down plates of canned meatballs with tomato sauce and dried cassava meal, the owner of the mining dredge arrives, a man known as Cabeludo, a nickname taken from the gray hair he ties back in a ponytail. For the miner, the boat and dredge are both his business and the home he shares with his wife, months-old baby, a cook and eight workers.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>He seems to take the uniformed intruders in stride, even when one pulls out a mobile phone and passes around a photo that is circulating in a WhatsApp group. It’s a corpse. Just hours earlier, he says, the owner of a mining dredge was murdered, allegedly by one of his workers, who fled into the jungle.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“He took half a kilo of gold with him,” one of the officers says.</p>
