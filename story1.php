<p>Mining machinery fills the lower level. On the upper deck, Getúlio’s living area boasts high-speed internet, surveillance cameras, a kitchen, bedrooms and a flat-screen TV equipped for streaming, offering him luxuries of city life even in the heart of the world’s largest tropical forest.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"align":"left","id":160277,"width":281,"height":157,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image alignleft size-full is-resized border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story1-img5.jpg" alt="" class="wp-image-160277" width="281" height="157"/><figcaption class="wp-element-caption">Gardens provide miners with fresh vegetables  <br>Alex Rufino</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>Living on the dredges for months at a time, miners give the structures touches of home, keeping pets, tending small gardens of lettuce and scallions, and raising chickens for food. Cooks bake fresh bread and keep the miners supplied with jugs of sweetened coffee.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Illegal mining is a magnet for men from impoverished communities—some Indigenous, some not— in the interior of</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>the Amazon, where education is poor and job options few. In the state of Amazonas, the average household earns less than the minimum wage of about US$ 250 a month. The prospect of a brighter economic future, leads workers to leave their families for months at a time to work on a draga, where they’re paid in gold.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“I like having a car and motorcycle and enjoying a Heineken at a nice restaurant,” one miner said. “I worked hard for it — it’s not to show off. If you had the chance, you would, too. Don’t judge us. It’s the only way to earn good money here.”</p>
<!-- /wp:paragraph -->

<!-- wp:spacer {"height":"32px"} -->
<div style="height:32px" aria-hidden="true" class="wp-block-spacer"></div>
<!-- /wp:spacer -->

<!-- wp:image {"id":160307,"sizeSlug":"full","linkDestination":"none"} -->
<figure class="wp-block-image size-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story1-icone3.png" alt="" class="wp-image-160307"/></figure>
<!-- /wp:image -->

<!-- wp:heading {"level":1} -->
<h1 class="wp-block-heading">ENVIRONMENTAL DAMAGE ‘IRREVERSIBLE’</h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>On both sides of the Colombian-Brazilian border where the Puré River becomes the Puruê, areas set aside to protect forests — and people living in them — are threatened by illegal mining on the river. On the Brazil side, the Puruê River falls under the jurisdiction of the Amazonas state government and the Brazilian Army, while the Juami River, a nearby mining hotspot, is inside the Juami-Japurá Ecological Station, a protected area managed by the federal government.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Besides the dredges that operate with impunity along the rivers, in some places miners with heavy equipment are pushing their way into the forest, threatening to turn the tropical landscape into a moonscape devoid of vegetation and pocked with water-filled craters. Calculations by the non-profit Amazon Conservation Team, which has an office in Brasília, indicate that about 1,010 hectares of forest have been destroyed by illegal mining along the Puruê River. More than half of that deforestation occurred in 2022.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":160274,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story1-img6.jpg" alt="" class="wp-image-160274"/><figcaption class="wp-element-caption">&nbsp;Mining has led to increased deforestation along the bank of the Puruê River | Alex Rufino</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>“There has been a general abandonment of the region in recent years, which has facilitated the entrance of all kinds of criminals, such as river pirates, drug dealers, and gold miners,” says Joel Araujo, who heads the government environmental agency IBAMA in the state of Amazonas. “Violence has grown a lot and criminality has taken the place of the state, co-opting young people and entire communities along the rivers.”</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>During the Bolsonaro administration, the country’s main environmental agencies were headed by police officials who dismantled infrastructure and closed checkpoints, including one near the Juami-Japurá Ecological Station. It was still closed in June, six months after Bolsonaro left office, but government officials said they planned to reopen it.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>On the Colombian side of the border, the government lost control over the Puré River after FARC dissidents ordered park rangers to abandon Puré National Park in 2020. “Because of the threats posed by the existence of the FARC, park rangers are unable to protect the parks,” the park’s director, Eliana Martínez Rueda says, adding that the guerrillas issued new threats and restrictions in January 2023.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Puré National Park was created in part to protect the Yuri-Passé, a semi-nomadic Indigenous group that shuns contact with outsiders. But with the rangers gone, dredges began to encroach on the area, and illegal mining continues there. The Colombian Navy, Army and Police did not respond to requests for information.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":160229,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story1-img7.jpg" alt="" class="wp-image-160229"/><figcaption class="wp-element-caption">Dredges eat away at the forested river bank, fill the water with silt and create mud flats of sediment | Andrés Cardona</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>Drug traffickers use the park’s rivers and there are clandestine airstrips in the area, Martínez says. Illegal miners also cross the border from Brazil into the park. “It’s already a no man’s land,” she adds, “and they enter Colombia easily.”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>She worries that the Yurí-Passé, a small tribe, could be wiped out by violence or disease if contacted by armed groups or illegal miners. “It’s a health risk, because [outsiders] can bring diseases for which [the Yuri-Passé] have not developed immunity,” she says.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":160150,"sizeSlug":"large","linkDestination":"none"} -->
<figure class="wp-block-image size-large"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/bg-green-1024x407.jpg" alt="" class="wp-image-160150"/></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>“Governments don’t have control or authority, so lots of these remote areas have allowed illegal mining groups and other illegal mafias to flourish, especially in transboundary regions,” says Brian Hettler, a geographer and cartographer with the non-profit Amazon Conservation Team, who has been mapping the presence of illegal mining dredges, says the environmental damage they cause is “irreversible.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“The environmental damage being done by illegal mining is largely irreversible,” he adds. “It will take many decades to dissipate, so these miners are literally changing the courses of rivers, altering the natural sedimentation processes developed over many decades.”</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":160271,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story1-img8.jpg" alt="" class="wp-image-160271"/><figcaption class="wp-element-caption">The movement of huge amounts of sediment changes both the color and the channel of Amazonian rivers | Andrés Cardona</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>Miners also dump mercury into rivers, where it is consumed by fish that forage along the river bed, then accumulates in the tissues of fish or other animals that feed on them. “The oldest and largest fish, typically at the top of the food chain, show the highest rates of contamination, and these are often the species that people like to eat, meaning that contamination is then passed on to the people,” Hettler says.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Mercury causes digestive and neurological problems and can cross the placenta, affecting a developing fetus. Some Amazonian fish travel long distances, so fish with high levels of mercury can affect people living far from mining areas, including isolated groups like the Yuri-Passé, Hettler says.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>A 2018 study of hair samples taken from Indigenous people in the Colombian Amazon near the Brazilian border found that about 90% of the community members had levels of mercury exceeding the maximum threshold recommended by the World Health Organization, in some cases by as much as four times.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":160268,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image size-full border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story1-img9.jpg" alt="" class="wp-image-160268"/><figcaption class="wp-element-caption">The mercury used by miners accumulates in the food chain, affecting people who depend on fish for protein, especially Indigenous communities | Alex Rufino</figcaption></figure>
<!-- /wp:image -->

<!-- wp:spacer {"height":"32px"} -->
<div style="height:32px" aria-hidden="true" class="wp-block-spacer"></div>
<!-- /wp:spacer -->

<!-- wp:image {"id":160304,"sizeSlug":"full","linkDestination":"none"} -->
<figure class="wp-block-image size-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story1-icone4.png" alt="" class="wp-image-160304"/></figure>
<!-- /wp:image -->

<!-- wp:heading {"level":1} -->
<h1 class="wp-block-heading">HIGH RISKS AND BIG PROFITS</h1>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Estimates of gold production for an industrial-scale dredge on Brazil’s Puruê River vary widely, but conservative calculations range from about 2.5 to 3 kilograms (5.5 to 6.6 pounds) a month. According to interviews with miners, law enforcement officials and gold traders in the state of Amazonas state, illegal gold is funneled through a complex network of intermediaries, gold shops and transporters.</p>
<!-- /wp:paragraph -->

<!-- wp:image {"align":"left","id":160265,"width":322,"height":176,"sizeSlug":"full","linkDestination":"none","className":"border-full"} -->
<figure class="wp-block-image alignleft size-full is-resized border-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story1-img10.jpg" alt="" class="wp-image-160265" width="322" height="176"/><figcaption class="wp-element-caption">A goldsmith in Tefé fashions a pendant in the shape<br>of Brazil | Rodrigo Pedroso</figcaption></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>Living on the dredges for months at a time, miners give the structures touches of home, keeping pets, tending small gardens of lettuce and scallions, and raising chickens for food. Cooks bake fresh bread and keep the miners supplied with jugs of sweetened coffee.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>A Military Police officer who asked to remain anonymous claims to escort shipments of illegally mined gold on private flights from the town of Japurá to Amazonian cities such as Porto Velho, Boa Vista and Manaus, where buyers receive the precious metal without inquiring about its origin.</p>
<!-- /wp:paragraph -->

<!-- wp:spacer {"height":"32px"} -->
<div style="height:32px" aria-hidden="true" class="wp-block-spacer"></div>
<!-- /wp:spacer -->

<!-- wp:heading -->
<h2 class="wp-block-heading" id="h-a-donde-se-va-el-camino-del-trafico-de-oro">A donde se va? El camino del tráfico de oro</h2>
<!-- /wp:heading -->

<!-- wp:paragraph {"className":"big"} -->
<p class="big">Extracción de oro en los ríos Juami y Puruê se exporta por Manaus y llega al mercado internacional</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":160262,"sizeSlug":"full","linkDestination":"none"} -->
<figure class="wp-block-image size-full"><img src="https://infoamazonia.org/wp-content/uploads/2023/07/story1-map.png" alt="" class="wp-image-160262"/></figure>
<!-- /wp:image -->

<!-- wp:spacer {"height":"16px"} -->
<div style="height:16px" aria-hidden="true" class="wp-block-spacer"></div>
<!-- /wp:spacer -->

<!-- wp:paragraph -->
<p>“The transport is simple. Every day, flights depart from Japurá and we safely transport the draga owner’s gold, which we call ‘big gold,’ to the capital where we can get the best price for it,” he says. Japurá’s airport has permission from Brazil's National Civil Aviation Agency to handle only private flights. Smugglers face a higher risk of detection when they land at larger airports where government inspectors are stationed.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The Military Police officer, who is from a large and economically poor family in the state of Amazonas, sees gold mining as a solution to poverty in the region.&nbsp; “It’s illegal, but not immoral,” he says. “It should be legalized, because now [miners] don’t pay taxes and the money doesn’t stay with the people of Amazonas. It ends up going abroad.”&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Before the gold leaves the Amazon, it usually is melted and molded into rectangular bars that weigh one kilogram (2.2 pounds) each and can fit in an adult’s hand.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Even if law enforcement operations destroy a dredge, the activity can still be profitable. According to miners, it costs about US$500,000 to US$600,000 to build a large draga in the Japurá region—a sum the owner of a highly productive dredge can recoup in a few months.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>For those involved, the potential payoff is worth the risk, and the huge amounts of money involved underscore the difficulty of reining in the illegal mining that has extended throughout the Amazon.</p>
<!-- /wp:paragraph -->

