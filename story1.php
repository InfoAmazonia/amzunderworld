<p>As Getúlio sips a fresh cup of coffee, thousands of gallons of muddy water clatter down a metal chute behind him. The miner owns two colossal dragas, which can produce kilos of gold a month, dwarfing the tens of grams produced by smaller dredges like Cabeludo’s, which are known as balsas. He knows that makes him a target.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>“The guerrillas came to this river, collecting taxes from miners,” Gétulio says. He digs out a receipt that says he made a&nbsp; “voluntary contribution” of 40 grams of gold. The small piece of paper, which features a photo of legendary FARC commander Mono Jojoy, notes that the gold was paid to the “Comisión Miller Perdomo,” which belongs to the FARC dissident group headed by a guerrilla leader known as Iván Mordisco.</p>
