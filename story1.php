<!-- wp:paragraph -->
<p>It’s a late afternoon in February, and three young men lounge on the deck of an <strong>old boat</strong> with peeling white paint moored along Brazil’s Puruê River, deep in the Amazon near the southernmost tip of Colombia. Tied to the boat is a battered, barn-like structure housing a tangle of chutes and gears and mounted on a barge. This is a mining dredge, one of scores along this narrow, winding river. Illegal and destructive, they churn up the riverbed, producing tens of millions of dollars’ worth of gold every month. This one is idle, though, awaiting repairs.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Reporters from Amazon Underworld are talking with the miners on the dredge, when just before sunset, the silence is broken by the sound of a powerful outboard motor as a smaller boat pulls up, a uniformed man wearing a balaclava and wielding a Brazilian-made assault rifle standing on the prow. He and five companions leap aboard the miners’ boat. Identifying themselves as a Brazilian Military Police river patrol, they pat down the miners, order them to fix a meal and settle in for the night.</p>
<!-- /wp:paragraph -->

<!-- wp:spacer {"height":"24px"} -->
